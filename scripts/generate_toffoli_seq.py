# CDKM algorithm: generate gate sequences
# ============================================================================


def generate_qq_gates(bits: int) -> list[ToffoliGate]:
    """Generate QQ CDKM adder gate sequence for given width.

    Qubit layout:
      [0..bits-1]       = register a (target)
      [bits..2*bits-1]  = register b (source)
      [2*bits]          = ancilla carry (bits >= 2)

    Width 1: single CX(target=0, control=1)
    Width N >= 2: MAJ/UMA chain with ancilla at 2*N
    """
    gates = []

    if bits == 1:
        # Single CX: a[0] ^= b[0]
        gates.append(ToffoliGate("X", target=0, controls=[1]))
        return gates

    ancilla = 2 * bits

    # Forward MAJ sweep
    # MAJ(ancilla, b[0], a[0])
    gates.extend(_maj(ancilla, bits + 0, 0))
    for i in range(1, bits):
        # MAJ(a[i-1], b[i], a[i])
        gates.extend(_maj(i - 1, bits + i, i))

    # Reverse UMA sweep
    for i in range(bits - 1, 0, -1):
        # UMA(a[i-1], b[i], a[i])
        gates.extend(_uma(i - 1, bits + i, i))
    # UMA(ancilla, b[0], a[0])
    gates.extend(_uma(ancilla, bits + 0, 0))

    return gates


def generate_cqq_gates(bits: int) -> list[ToffoliGate]:
    """Generate controlled QQ CDKM adder gate sequence for given width.

    Qubit layout:
      [0..bits-1]       = register a (target)
      [bits..2*bits-1]  = register b (source)
      [2*bits]          = ancilla carry (bits >= 2)
      [2*bits+1]        = external control qubit (bits >= 2)

    Width 1: [0]=a, [1]=b, [2]=ext_control. Single CCX(target=0, ctrl1=1, ctrl2=2)
    Width N >= 2: cMAJ/cUMA chain
    """
    gates = []

    if bits == 1:
        # Single CCX: controlled a[0] ^= b[0]
        gates.append(ToffoliGate("X", target=0, controls=[1, 2]))
        return gates

    ancilla = 2 * bits
    ext_ctrl = 2 * bits + 1

    # Forward cMAJ sweep
    gates.extend(_cmaj(ancilla, bits + 0, 0, ext_ctrl))
    for i in range(1, bits):
        gates.extend(_cmaj(i - 1, bits + i, i, ext_ctrl))

    # Reverse cUMA sweep
    for i in range(bits - 1, 0, -1):
        gates.extend(_cuma(i - 1, bits + i, i, ext_ctrl))
    gates.extend(_cuma(ancilla, bits + 0, 0, ext_ctrl))

    return gates


def _maj(a: int, b: int, c: int) -> list[ToffoliGate]:
    """MAJ(a, b, c): CX(b,c), CX(a,c), CCX(c,a,b)"""
    return [
        ToffoliGate("X", target=b, controls=[c]),  # CX(target=b, control=c)
        ToffoliGate("X", target=a, controls=[c]),  # CX(target=a, control=c)
        ToffoliGate("X", target=c, controls=[a, b]),  # CCX(target=c, ctrl1=a, ctrl2=b)
    ]


def _uma(a: int, b: int, c: int) -> list[ToffoliGate]:
    """UMA(a, b, c): CCX(c,a,b), CX(a,c), CX(b,a)"""
    return [
        ToffoliGate("X", target=c, controls=[a, b]),  # CCX(target=c, ctrl1=a, ctrl2=b)
        ToffoliGate("X", target=a, controls=[c]),  # CX(target=a, control=c)
        ToffoliGate("X", target=b, controls=[a]),  # CX(target=b, control=a)
    ]


def _cmaj(a: int, b: int, c: int, ext: int) -> list[ToffoliGate]:
    """cMAJ(a, b, c, ext): CCX(b,c,ext), CCX(a,c,ext), MCX(c,[a,b,ext])"""
    return [
        ToffoliGate("X", target=b, controls=[c, ext]),  # CCX
        ToffoliGate("X", target=a, controls=[c, ext]),  # CCX
        ToffoliGate("X", target=c, controls=[a, b, ext]),  # MCX(3 controls)
    ]


def _cuma(a: int, b: int, c: int, ext: int) -> list[ToffoliGate]:
    """cUMA(a, b, c, ext): MCX(c,[a,b,ext]), CCX(a,c,ext), CCX(b,a,ext)"""
    return [
        ToffoliGate("X", target=c, controls=[a, b, ext]),  # MCX(3 controls)
        ToffoliGate("X", target=a, controls=[c, ext]),  # CCX
        ToffoliGate("X", target=b, controls=[a, ext]),  # CCX
    ]


