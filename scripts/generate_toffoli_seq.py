#!/usr/bin/env python3
"""Toffoli hardcoded sequence generation script for CDKM addition (widths 1-8).

This script generates per-width C source files (toffoli_add_seq_N.c) and a unified
dispatch file (toffoli_add_seq_dispatch.c) for Toffoli-based addition sequences.

Generated files contain two addition variants per width:
  - QQ_add:  Quantum-quantum CDKM addition (static const sequence)
  - cQQ_add: Controlled quantum-quantum CDKM addition (dynamic init, cached)

The cQQ variant uses MCX(3 controls) which requires large_control heap allocation,
so it cannot be truly static const. Instead, a one-time init function builds and
caches the sequence.

Gate encoding:
  - CX:  .Gate=X, .Target=t, .NumControls=1, .Control={c}, .GateValue=1
  - CCX: .Gate=X, .Target=t, .NumControls=2, .Control={c1,c2}, .GateValue=1
  - MCX(3): Dynamically allocated with large_control for 3 controls

Algorithm: CDKM ripple-carry adder (Cuccaro et al., 2004)
  - MAJ(a,b,c) = CX(b,c), CX(a,c), CCX(c,a,b)
  - UMA(a,b,c) = CCX(c,a,b), CX(a,c), CX(b,a)
  - cMAJ(a,b,c,ext) = CCX(b,c,ext), CCX(a,c,ext), MCX(c,[a,b,ext])
  - cUMA(a,b,c,ext) = MCX(c,[a,b,ext]), CCX(a,c,ext), CCX(b,a,ext)

Usage:
  python scripts/generate_toffoli_seq.py                  # Generate all files
  python scripts/generate_toffoli_seq.py --width 4        # Generate only width 4
  python scripts/generate_toffoli_seq.py --dry-run        # Print to stdout
  python scripts/generate_toffoli_seq.py --validate       # Validate layer counts

Generated by: scripts/generate_toffoli_seq.py
Phase 72, Plan 01: Toffoli hardcoded sequence infrastructure
"""

