def generate_dispatch_file(max_width: int = 8) -> str:
    """Generate toffoli_add_seq_dispatch.c with unified dispatch functions."""
    output = []

    output.append("//")
    output.append(
        "// toffoli_add_seq_dispatch.c - Unified dispatch for hardcoded Toffoli addition sequences"
    )
    output.append("//")
    output.append("// Generated by scripts/generate_toffoli_seq.py - DO NOT EDIT MANUALLY")
    output.append("//")
    output.append("// Routes get_hardcoded_toffoli_*_add(bits) calls to per-width implementations.")
    output.append(
        "// Each per-width file is conditionally compiled via #ifdef TOFFOLI_SEQ_WIDTH_N."
    )
    output.append("//")
    output.append("")
    output.append('#include "toffoli_sequences.h"')
    output.append("#include <stddef.h>")
    output.append("")

    # Forward declarations
    output.append("// ============================================================================")
    output.append("// FORWARD DECLARATIONS (conditionally compiled per width)")
    output.append("// ============================================================================")
    output.append("")

    for w in range(1, max_width + 1):
        output.append(f"#ifdef TOFFOLI_SEQ_WIDTH_{w}")
        output.append(f"const sequence_t *get_hardcoded_toffoli_QQ_add_{w}(int bits);")
        output.append(f"const sequence_t *get_hardcoded_toffoli_cQQ_add_{w}(int bits);")
        output.append("#endif")
        output.append("")

    # QQ dispatch
    output.append("// ============================================================================")
    output.append("// UNIFIED TOFFOLI QQ_add DISPATCH")
    output.append("// ============================================================================")
    output.append("")
    output.append("const sequence_t *get_hardcoded_toffoli_QQ_add(int bits) {")
    output.append("    switch (bits) {")
    for w in range(1, max_width + 1):
        output.append(f"#ifdef TOFFOLI_SEQ_WIDTH_{w}")
        output.append(f"    case {w}: return get_hardcoded_toffoli_QQ_add_{w}(bits);")
        output.append("#endif")
    output.append("    default: return NULL;")
    output.append("    }")
    output.append("}")
    output.append("")

    # cQQ dispatch
    output.append("// ============================================================================")
    output.append("// UNIFIED TOFFOLI cQQ_add DISPATCH")
    output.append("// ============================================================================")
    output.append("")
    output.append("const sequence_t *get_hardcoded_toffoli_cQQ_add(int bits) {")
    output.append("    switch (bits) {")
    for w in range(1, max_width + 1):
        output.append(f"#ifdef TOFFOLI_SEQ_WIDTH_{w}")
        output.append(f"    case {w}: return get_hardcoded_toffoli_cQQ_add_{w}(bits);")
        output.append("#endif")
    output.append("    default: return NULL;")
    output.append("    }")
    output.append("}")
    output.append("")

    return "\n".join(output)


