# C code generation for cQQ (dynamic init with caching)
# ============================================================================


def generate_cqq_init_c(gates: list[ToffoliGate], width: int) -> str:
    """Generate dynamic init function for cQQ variant (has MCX with 3 controls)."""
    lines = []
    num_layers = len(gates)

    lines.append(f"static sequence_t *cached_toffoli_cQQ_{width} = NULL;")
    lines.append("")
    lines.append(f"static sequence_t *init_toffoli_cQQ_{width}(void) {{")
    lines.append(f"    if (cached_toffoli_cQQ_{width} != NULL) return cached_toffoli_cQQ_{width};")
    lines.append("")
    lines.append("    sequence_t *seq = malloc(sizeof(sequence_t));")
    lines.append("    if (seq == NULL) return NULL;")
    lines.append(f"    seq->num_layer = {num_layers};")
    lines.append(f"    seq->used_layer = {num_layers};")
    lines.append(f"    seq->gates_per_layer = calloc({num_layers}, sizeof(num_t));")
    lines.append(f"    seq->seq = calloc({num_layers}, sizeof(gate_t *));")
    lines.append("    if (seq->gates_per_layer == NULL || seq->seq == NULL) {")
    lines.append("        free(seq->gates_per_layer); free(seq->seq); free(seq); return NULL;")
    lines.append("    }")
    lines.append("")

    for i, gate in enumerate(gates):
        lines.append(f"    // Layer {i}: {_gate_description(gate)}")
        lines.append(f"    seq->gates_per_layer[{i}] = 1;")
        lines.append(f"    seq->seq[{i}] = calloc(1, sizeof(gate_t));")
        lines.append(f"    if (seq->seq[{i}] == NULL) goto cleanup;")
        lines.append(gate.to_c_dynamic(f"seq->seq[{i}][0]"))
        lines.append("")

    lines.append(f"    cached_toffoli_cQQ_{width} = seq;")
    lines.append("    return seq;")
    lines.append("")
    lines.append("cleanup:")
    lines.append("    for (int i = 0; i < " + str(num_layers) + "; i++) {")
    lines.append("        if (seq->seq[i] != NULL) {")
    lines.append("            if (seq->seq[i][0].large_control != NULL)")
    lines.append("                free(seq->seq[i][0].large_control);")
    lines.append("            free(seq->seq[i]);")
    lines.append("        }")
    lines.append("    }")
    lines.append("    free(seq->seq); free(seq->gates_per_layer); free(seq);")
    lines.append("    return NULL;")
    lines.append("}")

    return "\n".join(lines)


def _gate_description(gate: ToffoliGate) -> str:
    """Human-readable gate description for comments."""
    if gate.num_controls == 0:
        return f"X(target={gate.target})"
    elif gate.num_controls == 1:
        return f"CX(target={gate.target}, ctrl={gate.controls[0]})"
    elif gate.num_controls == 2:
        return f"CCX(target={gate.target}, ctrl1={gate.controls[0]}, ctrl2={gate.controls[1]})"
    else:
        ctrls = ",".join(str(c) for c in gate.controls)
        return f"MCX(target={gate.target}, ctrls=[{ctrls}])"


