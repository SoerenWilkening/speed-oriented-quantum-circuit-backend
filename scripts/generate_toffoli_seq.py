def generate_clifft_qq_width_file(width: int) -> str:
    """Generate complete C source file for QQ Clifford+T at one width."""
    output = []

    # Header
    output.append("//")
    output.append(
        f"// toffoli_clifft_qq_{width}.c - CDKM QQ Clifford+T addition for {width}-bit width"
    )
    output.append("//")
    output.append(
        "// Generated by scripts/generate_toffoli_seq.py --clifford-t - DO NOT EDIT MANUALLY"
    )
    output.append("//")
    output.append("// Contains: Toffoli CDKM QQ Clifford+T addition (static const, zero CCX)")
    output.append("// Algorithm: CDKM ripple-carry adder with CCX->Clifford+T decomposition")
    output.append("// All gates: H/T/Tdg/CX/X (max 1 control) -- fully static const.")
    output.append("//")
    output.append("")
    output.append('#include "toffoli_sequences.h"')
    output.append("")
    output.append(f"#ifdef TOFFOLI_SEQ_WIDTH_{width}")
    output.append("")
    output.append("#include <math.h>")
    output.append("")

    # Generate gate sequences
    qq_gates = generate_qq_gates(width)
    clifft_gates = toffoli_to_clifford_t(qq_gates)

    qq_num_qubits = 2 * width + (1 if width >= 2 else 0)
    output.append("// ============================================================================")
    output.append(f"// TOFFOLI CLIFFT QQ_ADD WIDTH {width} ({len(clifft_gates)} layers)")
    if width == 1:
        output.append("// Qubit layout: [0] = target, [1] = source")
    else:
        output.append(
            f"// Qubit layout: [0,{width - 1}] = target (a), "
            f"[{width},{2 * width - 1}] = source (b), [{2 * width}] = ancilla"
        )
    output.append(f"// Total qubits: {qq_num_qubits}")
    output.append("// All gates: H/T/Tdg/CX/X (max 1 control) -- zero CCX")
    output.append("// ============================================================================")
    output.append("")
    output.append(generate_clifft_qq_static_c(clifft_gates, width))
    output.append("")

    # Dispatch helper
    output.append("// ============================================================================")
    output.append(f"// DISPATCH HELPER FOR WIDTH {width}")
    output.append("// ============================================================================")
    output.append("")
    output.append(f"const sequence_t *get_hardcoded_toffoli_clifft_QQ_add_{width}(int bits) {{")
    output.append("    (void)bits;")
    output.append(f"    return &HARDCODED_TOFFOLI_CLIFFT_QQ_{width};")
    output.append("}")
    output.append("")

    output.append(f"#endif // TOFFOLI_SEQ_WIDTH_{width}")
    output.append("")

    return "\n".join(output)


