def main():
    parser = argparse.ArgumentParser(
        description="Generate hardcoded Toffoli addition sequence C files for widths 1-8."
    )
    parser.add_argument(
        "--width",
        type=int,
        metavar="N",
        help="Generate only for width N (default: all 1-8)",
    )
    parser.add_argument(
        "--dry-run",
        action="store_true",
        help="Print to stdout instead of writing files",
    )
    parser.add_argument(
        "--output-dir",
        type=str,
        default=None,
        help="Output directory for C files (default: c_backend/src/sequences/)",
    )
    parser.add_argument(
        "--header-dir",
        type=str,
        default=None,
        help="Output directory for header file (default: c_backend/include/)",
    )
    parser.add_argument(
        "--validate",
        action="store_true",
        help="Cross-validate against expected layer counts",
    )
    parser.add_argument(
        "--max-width",
        type=int,
        default=8,
        help="Maximum width to generate (default: 8)",
    )
    parser.add_argument(
        "--clifford-t",
        action="store_true",
        help="Generate Clifford+T decomposed sequences (CCX expanded to 15 H/T/Tdg/CX gates)",
    )

    args = parser.parse_args()

    if args.validate and args.clifford_t:
        ok = validate_clifford_t(args.max_width)
        sys.exit(0 if ok else 1)

    if args.validate:
        ok = validate(args.max_width)
        sys.exit(0 if ok else 1)

    # Determine output directories
    script_dir = os.path.dirname(os.path.abspath(__file__))

    if args.output_dir:
        output_dir = args.output_dir
    else:
        output_dir = os.path.normpath(
            os.path.join(script_dir, "..", "c_backend", "src", "sequences")
        )

    if args.header_dir:
        header_dir = args.header_dir
    else:
        header_dir = os.path.normpath(os.path.join(script_dir, "..", "c_backend", "include"))

    max_width = args.max_width

    if args.clifford_t:
        # Clifford+T QQ generation mode
        if args.width:
            if args.width < 1 or args.width > max_width:
                print(f"Error: width must be 1-{max_width}", file=sys.stderr)
                sys.exit(1)
            content = generate_clifft_qq_width_file(args.width)
            if args.dry_run:
                print(content)
            else:
                os.makedirs(output_dir, exist_ok=True)
                path = os.path.join(output_dir, f"toffoli_clifft_qq_{args.width}.c")
                with open(path, "w") as f:
                    f.write(content)
                print(f"Written: {path}")
        else:
            # Full Clifford+T QQ generation
            if args.dry_run:
                for w in range(1, max_width + 1):
                    print(f"// === FILE: toffoli_clifft_qq_{w}.c ===")
                    print(generate_clifft_qq_width_file(w))
                    print()
            else:
                os.makedirs(output_dir, exist_ok=True)
                for w in range(1, max_width + 1):
                    content = generate_clifft_qq_width_file(w)
                    path = os.path.join(output_dir, f"toffoli_clifft_qq_{w}.c")
                    with open(path, "w") as f:
                        f.write(content)
                    print(f"Written: {path}")
    elif args.width:
        # Single width mode
        if args.width < 1 or args.width > max_width:
            print(f"Error: width must be 1-{max_width}", file=sys.stderr)
            sys.exit(1)
        content = generate_width_file(args.width)
        if args.dry_run:
            print(content)
        else:
            os.makedirs(output_dir, exist_ok=True)
            path = os.path.join(output_dir, f"toffoli_add_seq_{args.width}.c")
            with open(path, "w") as f:
                f.write(content)
            print(f"Written: {path}")
    else:
        # Full generation mode
        if args.dry_run:
            print("// === FILE: toffoli_sequences.h ===")
            print(generate_header_file(max_width))
            print()
            for w in range(1, max_width + 1):
                print(f"// === FILE: toffoli_add_seq_{w}.c ===")
                print(generate_width_file(w))
                print()
            print("// === FILE: toffoli_add_seq_dispatch.c ===")
            print(generate_dispatch_file(max_width))
        else:
            # Write header
            os.makedirs(header_dir, exist_ok=True)
            header_path = os.path.join(header_dir, "toffoli_sequences.h")
            with open(header_path, "w") as f:
                f.write(generate_header_file(max_width))
            print(f"Written: {header_path}")

            # Write per-width files
            os.makedirs(output_dir, exist_ok=True)
            for w in range(1, max_width + 1):
                content = generate_width_file(w)
                path = os.path.join(output_dir, f"toffoli_add_seq_{w}.c")
                with open(path, "w") as f:
                    f.write(content)
                print(f"Written: {path}")

            # Write dispatch file
            dispatch_content = generate_dispatch_file(max_width)
            dispatch_path = os.path.join(output_dir, "toffoli_add_seq_dispatch.c")
            with open(dispatch_path, "w") as f:
                f.write(dispatch_content)
            print(f"Written: {dispatch_path}")

    return 0


if __name__ == "__main__":
    sys.exit(main() or 0)
