def generate_header_file(max_width: int = 8) -> str:
    """Generate toffoli_sequences.h header file."""
    output = []

    output.append("//")
    output.append("// toffoli_sequences.h - Hardcoded Toffoli gate sequences for CDKM addition")
    output.append("//")
    output.append("// This module provides pre-computed Toffoli gate sequences for CDKM")
    output.append(f"// ripple-carry addition up to {max_width}-bit width. These avoid runtime")
    output.append("// malloc/build overhead for the most common Toffoli addition widths.")
    output.append("//")
    output.append("// Two variants are supported:")
    output.append("//   QQ_add  - Quantum-quantum CDKM addition (static const)")
    output.append("//   cQQ_add - Controlled quantum-quantum CDKM addition (dynamic init, cached)")
    output.append("//")
    output.append("// PUBLIC API:")
    output.append(
        "//   get_hardcoded_toffoli_QQ_add(bits)  - Returns Toffoli QQ_add sequence or NULL"
    )
    output.append(
        "//   get_hardcoded_toffoli_cQQ_add(bits) - Returns Toffoli cQQ_add sequence or NULL"
    )
    output.append("//")
    output.append(f"// Returns NULL for widths > TOFFOLI_HARDCODED_MAX_WIDTH ({max_width}).")
    output.append("// Caller must fall back to dynamic generation for NULL returns.")
    output.append("//")
    output.append("// IMPLEMENTATION:")
    output.append(
        "//   - Per-width files: toffoli_add_seq_N.c (N=1..8), via #ifdef TOFFOLI_SEQ_WIDTH_N"
    )
    output.append("//   - Unified dispatch: toffoli_add_seq_dispatch.c routes to per-width impls")
    output.append("//   - Define TOFFOLI_SEQ_NO_WIDTH_N before including to disable width N")
    output.append("//")
    output.append("// Generated by scripts/generate_toffoli_seq.py - DO NOT EDIT MANUALLY")
    output.append("//")
    output.append("")
    output.append("#ifndef QUANTUM_TOFFOLI_SEQUENCES_H")
    output.append("#define QUANTUM_TOFFOLI_SEQUENCES_H")
    output.append("")
    output.append('#include "types.h"')
    output.append("")

    # Preprocessor guards
    output.append("// ======================================================")
    output.append("// PREPROCESSOR GUARDS - All widths enabled by default")
    output.append("// Define TOFFOLI_SEQ_NO_WIDTH_N before including to disable width N")
    output.append("// ======================================================")
    output.append("")

    for w in range(1, max_width + 1):
        output.append(f"#ifndef TOFFOLI_SEQ_NO_WIDTH_{w}")
        output.append(f"#define TOFFOLI_SEQ_WIDTH_{w}")
        output.append("#endif")

    output.append("")
    output.append("// Maximum width for which hardcoded Toffoli sequences are available")
    output.append(f"#define TOFFOLI_HARDCODED_MAX_WIDTH {max_width}")
    output.append("")

    # Public API
    output.append("// ======================================================")
    output.append("// PUBLIC API - 2 dispatch functions")
    output.append("// ======================================================")
    output.append("")
    output.append("// Returns pre-computed Toffoli QQ_add sequence for given bit width")
    output.append("// Returns NULL if bits > TOFFOLI_HARDCODED_MAX_WIDTH or bits < 1")
    output.append("const sequence_t *get_hardcoded_toffoli_QQ_add(int bits);")
    output.append("")
    output.append("// Returns pre-computed Toffoli cQQ_add (controlled QQ_add) sequence")
    output.append("// Returns NULL if bits > TOFFOLI_HARDCODED_MAX_WIDTH or bits < 1")
    output.append("const sequence_t *get_hardcoded_toffoli_cQQ_add(int bits);")
    output.append("")
    output.append("#endif // QUANTUM_TOFFOLI_SEQUENCES_H")
    output.append("")

    return "\n".join(output)


