    output.append('#include "toffoli_sequences.h"')
    output.append("")
    output.append(f"#ifdef TOFFOLI_SEQ_WIDTH_{width}")
    output.append("")
    output.append("#include <stdlib.h>")
    output.append("")

    # Generate gate sequences
    qq_gates = generate_qq_gates(width)
    cqq_gates = generate_cqq_gates(width)

    # QQ section (static const)
    qq_num_qubits = 2 * width + (1 if width >= 2 else 0)
    output.append("// ============================================================================")
    output.append(f"// TOFFOLI QQ_ADD WIDTH {width} ({len(qq_gates)} layers)")
    if width == 1:
        output.append("// Qubit layout: [0] = target, [1] = source")
    else:
        output.append(
            f"// Qubit layout: [0,{width - 1}] = target (a), "
            f"[{width},{2 * width - 1}] = source (b), [{2 * width}] = ancilla"
        )
    output.append(f"// Total qubits: {qq_num_qubits}")
    output.append("// ============================================================================")
    output.append("")
    output.append(generate_qq_static_c(qq_gates, width))
    output.append("")

    # cQQ section (dynamic init)
    if width == 1:
        cqq_num_qubits = 3
    else:
        cqq_num_qubits = 2 * width + 2
    output.append("// ============================================================================")
    output.append(f"// TOFFOLI cQQ_ADD WIDTH {width} ({len(cqq_gates)} layers)")
    if width == 1:
        output.append("// Qubit layout: [0] = target, [1] = source, [2] = ext_ctrl")
    else:
        output.append(
            f"// Qubit layout: [0,{width - 1}] = target (a), "
            f"[{width},{2 * width - 1}] = source (b), "
            f"[{2 * width}] = ancilla, [{2 * width + 1}] = ext_ctrl"
        )
    output.append(f"// Total qubits: {cqq_num_qubits}")
    output.append("// ============================================================================")
    output.append("")
    output.append(generate_cqq_init_c(cqq_gates, width))
    output.append("")

    # Dispatch helpers
    output.append("// ============================================================================")
    output.append(f"// DISPATCH HELPERS FOR WIDTH {width}")
    output.append("// ============================================================================")
    output.append("")
    output.append(f"const sequence_t *get_hardcoded_toffoli_QQ_add_{width}(int bits) {{")
    output.append("    (void)bits;")
    output.append(f"    return &HARDCODED_TOFFOLI_QQ_{width};")
    output.append("}")
    output.append("")
    output.append(f"const sequence_t *get_hardcoded_toffoli_cQQ_add_{width}(int bits) {{")
    output.append("    (void)bits;")
    output.append(f"    return (const sequence_t *)init_toffoli_cQQ_{width}();")
    output.append("}")
    output.append("")

    output.append(f"#endif // TOFFOLI_SEQ_WIDTH_{width}")
    output.append("")

    return "\n".join(output)


