@dataclass
class ToffoliGate:
    """Represents a single gate in a Toffoli sequence."""

    gate_type: str  # "X" (with controls: CX, CCX, MCX)
    target: int
    controls: list[int] = field(default_factory=list)

    @property
    def num_controls(self) -> int:
        return len(self.controls)

    def to_c_static(self) -> str:
        """Generate C static const initializer (only for gates with <= 2 controls)."""
        if self.num_controls == 0:
            # X gate (no controls)
            return (
                f"{{.Gate = X,\n"
                f"                                      .Target = {self.target},\n"
                f"                                      .NumControls = 0,\n"
                f"                                      .Control = {{0}},\n"
                f"                                      .large_control = NULL,\n"
                f"                                      .GateValue = 1,\n"
                f"                                      .NumBasisGates = 0}}"
            )
        elif self.num_controls == 1:
            # CX gate
            return (
                f"{{.Gate = X,\n"
                f"                                      .Target = {self.target},\n"
                f"                                      .NumControls = 1,\n"
                f"                                      .Control = {{{self.controls[0]}}},\n"
                f"                                      .large_control = NULL,\n"
                f"                                      .GateValue = 1,\n"
                f"                                      .NumBasisGates = 0}}"
            )
        elif self.num_controls == 2:
            # CCX gate
            return (
                f"{{.Gate = X,\n"
                f"                                      .Target = {self.target},\n"
                f"                                      .NumControls = 2,\n"
                f"                                      .Control = {{{self.controls[0]}, {self.controls[1]}}},\n"
                f"                                      .large_control = NULL,\n"
                f"                                      .GateValue = 1,\n"
                f"                                      .NumBasisGates = 0}}"
            )
        else:
            raise ValueError(f"Cannot generate static const for {self.num_controls} controls")

    def to_c_dynamic(self, var_name: str) -> str:
        """Generate C dynamic initialization code for a gate (any number of controls)."""
        lines = []
        if self.num_controls <= 2:
            ctrl_str = ", ".join(str(c) for c in self.controls) if self.controls else "0"
            if self.num_controls < 2:
                ctrl_init = f"{{{ctrl_str}}}"
            else:
                ctrl_init = f"{{{ctrl_str}}}"
            lines.append(
                f"    {var_name} = (gate_t){{.Gate = X, .Target = {self.target}, "
                f".NumControls = {self.num_controls}, .Control = {ctrl_init}, "
                f".large_control = NULL, .GateValue = 1, .NumBasisGates = 0}};"
            )
        else:
            # MCX with 3+ controls: need large_control allocation
            lines.append(f"    {var_name}.Gate = X;")
            lines.append(f"    {var_name}.Target = {self.target};")
            lines.append(f"    {var_name}.NumControls = {self.num_controls};")
            lines.append(f"    {var_name}.Control[0] = {self.controls[0]};")
            lines.append(f"    {var_name}.Control[1] = {self.controls[1]};")
            lines.append(f"    {var_name}.GateValue = 1;")
            lines.append(f"    {var_name}.NumBasisGates = 0;")
            lines.append(
                f"    {var_name}.large_control = malloc({self.num_controls} * sizeof(qubit_t));"
            )
            for i, c in enumerate(self.controls):
                lines.append(f"    {var_name}.large_control[{i}] = (qubit_t){c};")
        return "\n".join(lines)


