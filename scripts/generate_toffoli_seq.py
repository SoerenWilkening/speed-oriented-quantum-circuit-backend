def validate_clifford_t(max_width: int = 8) -> bool:
    """Validate Clifford+T QQ generated gate sequences against expected counts."""
    all_ok = True

    # Expected Clifford+T QQ layer counts:
    # Width 1: 1 gate (single CX, no CCX to expand)
    # Width N >= 2: 4N CX + 4N*15 CCX = 4N + 60N = 64N layers
    print("=" * 60)
    print("Clifford+T QQ_add layer count validation")
    print("=" * 60)
    for w in range(1, max_width + 1):
        qq_gates = generate_qq_gates(w)
        clifft_gates = toffoli_to_clifford_t(qq_gates)
        actual = len(clifft_gates)
        if w == 1:
            expected = 1  # single CX
        else:
            # Count CX and CCX in original
            cx_count = sum(1 for g in qq_gates if g.num_controls <= 1)
            ccx_count = sum(1 for g in qq_gates if g.num_controls == 2)
            expected = cx_count + ccx_count * 15
        status = "OK" if actual == expected else "MISMATCH"
        if actual != expected:
            all_ok = False
        print(f"  width {w:2d}: {actual:4d} layers  expected {expected:4d}  [{status}]")

    print()

    # Verify all Clifford+T gates have max 1 control
    print("=" * 60)
    print("Clifford+T QQ gate control count validation (must be <= 1)")
    print("=" * 60)
    for w in range(1, max_width + 1):
        qq_gates = generate_qq_gates(w)
        clifft_gates = toffoli_to_clifford_t(qq_gates)
        max_ctrl = 0
        for g in clifft_gates:
            ctrl = 1 if g.gate_type == "CX" else 0
            max_ctrl = max(max_ctrl, ctrl)
        ccx_count = sum(1 for g in clifft_gates if g.gate_type == "CX" and False)  # always 0
        status = "OK" if max_ctrl <= 1 else "FAIL"
        if max_ctrl > 1:
            all_ok = False
        print(f"  width {w:2d}: max controls = {max_ctrl}  [{status}]")

    print()

    # Verify gate type distribution
    print("=" * 60)
    print("Clifford+T QQ gate type distribution")
    print("=" * 60)
    for w in range(1, max_width + 1):
        qq_gates = generate_qq_gates(w)
        clifft_gates = toffoli_to_clifford_t(qq_gates)
        counts = {"H": 0, "T": 0, "Tdg": 0, "X": 0, "CX": 0}
        for g in clifft_gates:
            counts[g.gate_type] += 1
        parts = ", ".join(f"{k}={v}" for k, v in counts.items() if v > 0)
        print(f"  width {w:2d}: {parts}, total={len(clifft_gates)}")

    print()

    if all_ok:
        print("ALL CLIFFORD+T QQ VALIDATIONS PASSED")
    else:
        print("CLIFFORD+T QQ VALIDATION FAILED - see mismatches above")

    return all_ok


def validate(max_width: int = 8) -> bool:
    """Validate generated gate sequences against expected counts."""
    all_ok = True

    # Expected QQ layer counts:
    # Width 1: 1 gate (single CX)
    # Width N >= 2: 6*N gates (N MAJ + N UMA, each 3 gates)
    print("=" * 60)
    print("Toffoli QQ_add layer count validation")
    print("=" * 60)
    for w in range(1, max_width + 1):
        gates = generate_qq_gates(w)
        actual = len(gates)
        expected = 1 if w == 1 else 6 * w
        status = "OK" if actual == expected else "MISMATCH"
        if actual != expected:
            all_ok = False
        print(f"  width {w:2d}: {actual:4d} layers  expected {expected:4d}  [{status}]")

    print()

    # Expected cQQ layer counts:
    # Width 1: 1 gate (single CCX)
    # Width N >= 2: 6*N gates (N cMAJ + N cUMA, each 3 gates)
    print("=" * 60)
    print("Toffoli cQQ_add layer count validation")
    print("=" * 60)
    for w in range(1, max_width + 1):
        gates = generate_cqq_gates(w)
        actual = len(gates)
        expected = 1 if w == 1 else 6 * w
        status = "OK" if actual == expected else "MISMATCH"
        if actual != expected:
            all_ok = False
        print(f"  width {w:2d}: {actual:4d} layers  expected {expected:4d}  [{status}]")

    print()

    # Verify QQ gates use only CX and CCX (max 2 controls)
    print("=" * 60)
    print("QQ gate control count validation (must be <= 2)")
    print("=" * 60)
    for w in range(1, max_width + 1):
        gates = generate_qq_gates(w)
        max_ctrl = max(g.num_controls for g in gates)
        status = "OK" if max_ctrl <= 2 else "FAIL"
        if max_ctrl > 2:
            all_ok = False
        print(f"  width {w:2d}: max controls = {max_ctrl}  [{status}]")

    print()

    # Verify cQQ gates with MCX (3 controls) for width >= 2
    print("=" * 60)
    print("cQQ gate control count validation")
    print("=" * 60)
    for w in range(1, max_width + 1):
        gates = generate_cqq_gates(w)
        max_ctrl = max(g.num_controls for g in gates)
        mcx_count = sum(1 for g in gates if g.num_controls == 3)
        if w == 1:
            expected_max = 2  # Single CCX
            expected_mcx = 0
        else:
            expected_max = 3  # MCX from cMAJ/cUMA
            expected_mcx = 2 * w  # 1 MCX per cMAJ + 1 per cUMA, N of each
        status = "OK" if max_ctrl == expected_max and mcx_count == expected_mcx else "MISMATCH"
        if max_ctrl != expected_max or mcx_count != expected_mcx:
            all_ok = False
        print(
            f"  width {w:2d}: max controls = {max_ctrl}, "
            f"MCX(3) count = {mcx_count} (expected {expected_mcx})  [{status}]"
        )

    print()

    # Verify gate sequence matches ToffoliAddition.c patterns
    print("=" * 60)
    print("Width 2 QQ gate sequence verification (should match CDKM)")
    print("=" * 60)
    gates = generate_qq_gates(2)
    # Width 2 QQ: MAJ(4,2,0), MAJ(0,3,1), UMA(0,3,1), UMA(4,2,0)
    # ancilla=4, b[0]=2, a[0]=0, b[1]=3, a[1]=1
    expected_seq = [
        # MAJ(4, 2, 0)
        ("CX", 2, [0]),  # CX(target=b, control=c) = CX(2, 0)
        ("CX", 4, [0]),  # CX(target=a, control=c) = CX(4, 0)
        ("CCX", 0, [4, 2]),  # CCX(target=c, ctrl=a, ctrl=b) = CCX(0, 4, 2)
        # MAJ(0, 3, 1)
        ("CX", 3, [1]),
        ("CX", 0, [1]),
        ("CCX", 1, [0, 3]),
        # UMA(0, 3, 1)
        ("CCX", 1, [0, 3]),
        ("CX", 0, [1]),
        ("CX", 3, [0]),
        # UMA(4, 2, 0)
        ("CCX", 0, [4, 2]),
        ("CX", 4, [0]),
        ("CX", 2, [4]),
    ]

    all_match = True
    for i, (_exp_type, exp_target, exp_ctrls) in enumerate(expected_seq):
        gate = gates[i]
        match = gate.target == exp_target and gate.controls == exp_ctrls
        if not match:
            all_match = False
            print(
                f"  Layer {i}: MISMATCH - got ({gate.target}, {gate.controls}), "
                f"expected ({exp_target}, {exp_ctrls})"
            )

    if all_match:
        print("  All 12 gates match expected CDKM pattern")
    else:
        all_ok = False

    print()
    if all_ok:
        print("ALL VALIDATIONS PASSED")
    else:
        print("VALIDATION FAILED - see mismatches above")

    return all_ok


