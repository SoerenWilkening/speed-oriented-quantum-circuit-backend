# BK CLA QQ Clifford+T generation
# ============================================================================


def generate_bk_cla_qq_gates(bits: int) -> list[ToffoliGate]:
    """Generate BK CLA QQ adder gate sequence (X/CX/CCX).

    Qubit layout:
      a[i] = i               (0..n-1)
      b[i] = n+i             (n..2n-1)
      g[i] = 2n+i            (2n..3n-2)  generate ancilla
      tree_anc = 3n-1..       tree propagate-product ancilla
      carry_copy = 3n-1+num_merges..  carry-copy ancilla

    Phase A: Initialize generates and propagates
    Phase B: BK prefix tree
    Phase C: Copy carries
    Phase D: Reverse Phase B
    Phase E: Uncompute propagates and generates
    Phase F: Sum extraction
    """
    if bits < 2:
        return []

    n = bits
    n_carries = n - 1

    merges = bk_compute_merges(n_carries)
    num_merges = len(merges)

    tree_base = 3 * n - 1
    carry_base = tree_base + num_merges

    # Track propagate source for each carry position
    prop_src = [n + i for i in range(n_carries)]  # Initially b[i]

    gates = []

    # Phase A: Initialize generate and propagate
    for i in range(n_carries):
        gates.append(ToffoliGate("CCX", 2 * n + i, [i, n + i]))  # CCX(g[i], a[i], b[i])
    for i in range(n):
        gates.append(ToffoliGate("CX", n + i, [i]))  # CX(b[i], a[i])

    # Phase B: BK prefix tree
    phase_b_gates = []
    tree_anc_idx = 0
    for m_pos, m_partner in merges:
        tree_qubit = tree_base + tree_anc_idx

        # Step 1: Update group generate
        g1 = ToffoliGate("CCX", 2 * n + m_pos, [prop_src[m_pos], 2 * n + m_partner])
        gates.append(g1)
        phase_b_gates.append(g1)

        # Step 2: Compute group propagate product
        g2 = ToffoliGate("CCX", tree_qubit, [prop_src[m_pos], prop_src[m_partner]])
        gates.append(g2)
        phase_b_gates.append(g2)

        # Update prop_src
        prop_src[m_pos] = tree_qubit
        tree_anc_idx += 1

    # Phase C: Copy carries
    for i in range(n_carries):
        gates.append(ToffoliGate("CX", carry_base + i, [2 * n + i]))  # CX(c[i], g[i])

    # Phase D: Reverse Phase B
    for g in reversed(phase_b_gates):
        gates.append(ToffoliGate(g.gate_type, g.target, list(g.controls)))

    # Phase E: Uncompute propagates and generates
    for i in range(n - 1, -1, -1):
        gates.append(ToffoliGate("CX", n + i, [i]))  # CX(b[i], a[i])
    for i in range(n_carries - 1, -1, -1):
        gates.append(ToffoliGate("CCX", 2 * n + i, [i, n + i]))  # CCX(g[i], a[i], b[i])

    # Phase F: Sum extraction
    for i in range(n):
        gates.append(ToffoliGate("CX", n + i, [i]))  # CX(b[i], a[i])
    for i in range(1, n):
        gates.append(ToffoliGate("CX", n + i, [carry_base + i - 1]))  # CX(b[i], c[i-1])

    return gates


def generate_bk_cla_qq_clifft(bits: int) -> list[CliffordTGate]:
    """Generate BK CLA QQ Clifford+T sequence."""
    toffoli_gates = generate_bk_cla_qq_gates(bits)
    return expand_to_clifford_t(toffoli_gates)


