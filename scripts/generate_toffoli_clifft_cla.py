#!/usr/bin/env python3
"""BK CLA Clifford+T hardcoded sequence generation (widths 2-8).

This script generates per-width C source files and a unified dispatch file
for Clifford+T decomposed Brent-Kung Carry Look-Ahead addition sequences.

Four variants are supported:
  QQ      - Quantum-quantum BK CLA addition
  cQQ     - Controlled quantum-quantum BK CLA addition
  CQ_inc  - Classical-quantum increment (value=1) BK CLA
  cCQ_inc - Controlled classical-quantum increment (value=1) BK CLA

Width 1 is excluded because BK CLA returns NULL for bits < 2 (falls back to
CDKM/RCA at width 1).

CCX -> Clifford+T decomposition (15 gates per CCX):
  H(t), CX(t,c2), Tdg(t), CX(t,c1), T(t), CX(t,c2), Tdg(t), CX(t,c1),
  T(t), T(c2), H(t), CX(c2,c1), T(c1), Tdg(c2), CX(c2,c1)
  = 2H + 4T + 3Tdg + 6CX

Gate encoding in generated C:
  - H:   .Gate=H,       .Target=t, .NumControls=0, .Control={0}, .GateValue=0
  - T:   .Gate=T_GATE,  .Target=t, .NumControls=0, .Control={0}, .GateValue=M_PI/4.0
  - Tdg: .Gate=TDG_GATE,.Target=t, .NumControls=0, .Control={0}, .GateValue=-M_PI/4.0
  - X:   .Gate=X,       .Target=t, .NumControls=0, .Control={0}, .GateValue=1
  - CX:  .Gate=X,       .Target=t, .NumControls=1, .Control={c}, .GateValue=1

Algorithm: Brent-Kung parallel prefix CLA adder (Brent & Kung, 1982)
  Phase A: Initialize single-bit generates and propagates
  Phase B: BK prefix tree (up-sweep + down-sweep)
  Phase C: Copy carries from generate ancilla to carry-copy ancilla
  Phase D: Reverse Phase B (uncompute tree)
  Phase E: Uncompute propagates and generates
  Phase F: Sum extraction using carries

Generated by: scripts/generate_toffoli_clifft_cla.py
Phase 75, Plan 02: BK CLA Clifford+T hardcoded sequence generation
"""

import argparse
import os
import sys
from dataclasses import dataclass, field

# ============================================================================
# CliffordTGate dataclass
# ============================================================================


@dataclass
class CliffordTGate:
    """Represents a single Clifford+T gate (max 1 control)."""

    gate_type: str  # "H", "T", "Tdg", "X", "CX"
    target: int
    controls: list[int] = field(default_factory=list)

    @property
    def num_controls(self) -> int:
        return len(self.controls)

    def to_c_static(self) -> str:
        """Generate C static const initializer."""
        if self.gate_type == "H":
            return (
                f"{{.Gate = H,\n"
                f"                                      .Target = {self.target},\n"
                f"                                      .NumControls = 0,\n"
                f"                                      .Control = {{0}},\n"
                f"                                      .large_control = NULL,\n"
                f"                                      .GateValue = 0,\n"
                f"                                      .NumBasisGates = 0}}"
            )
        elif self.gate_type == "T":
            return (
                f"{{.Gate = T_GATE,\n"
                f"                                      .Target = {self.target},\n"
                f"                                      .NumControls = 0,\n"
                f"                                      .Control = {{0}},\n"
                f"                                      .large_control = NULL,\n"
                f"                                      .GateValue = M_PI/4.0,\n"
                f"                                      .NumBasisGates = 0}}"
            )
        elif self.gate_type == "Tdg":
            return (
                f"{{.Gate = TDG_GATE,\n"
                f"                                      .Target = {self.target},\n"
                f"                                      .NumControls = 0,\n"
                f"                                      .Control = {{0}},\n"
                f"                                      .large_control = NULL,\n"
                f"                                      .GateValue = -M_PI/4.0,\n"
                f"                                      .NumBasisGates = 0}}"
            )
        elif self.gate_type == "X":
            if self.num_controls == 0:
                return (
                    f"{{.Gate = X,\n"
                    f"                                      .Target = {self.target},\n"
                    f"                                      .NumControls = 0,\n"
                    f"                                      .Control = {{0}},\n"
                    f"                                      .large_control = NULL,\n"
                    f"                                      .GateValue = 1,\n"
                    f"                                      .NumBasisGates = 0}}"
                )
            else:
                raise ValueError("X gate with controls not supported; use CX")
        elif self.gate_type == "CX":
            if self.num_controls != 1:
                raise ValueError(f"CX must have exactly 1 control, got {self.num_controls}")
            return (
                f"{{.Gate = X,\n"
                f"                                      .Target = {self.target},\n"
                f"                                      .NumControls = 1,\n"
                f"                                      .Control = {{{self.controls[0]}}},\n"
                f"                                      .large_control = NULL,\n"
                f"                                      .GateValue = 1,\n"
                f"                                      .NumBasisGates = 0}}"
            )
        else:
            raise ValueError(f"Unknown gate type: {self.gate_type}")


# ============================================================================
# CCX -> Clifford+T expansion
# ============================================================================


def ccx_to_clifford_t(target: int, ctrl1: int, ctrl2: int) -> list[CliffordTGate]:
    """Expand one CCX into 15 Clifford+T gates.

    Matches emit_ccx_clifford_t_seq() from gate.c exactly:
      H(t), CX(t,c2), Tdg(t), CX(t,c1), T(t), CX(t,c2), Tdg(t), CX(t,c1),
      T(t), T(c2), H(t), CX(c2,c1), T(c1), Tdg(c2), CX(c2,c1)
    """
    return [
        CliffordTGate("H", target),
        CliffordTGate("CX", target, [ctrl2]),
        CliffordTGate("Tdg", target),
        CliffordTGate("CX", target, [ctrl1]),
        CliffordTGate("T", target),
        CliffordTGate("CX", target, [ctrl2]),
        CliffordTGate("Tdg", target),
        CliffordTGate("CX", target, [ctrl1]),
        CliffordTGate("T", target),
        CliffordTGate("T", ctrl2),
        CliffordTGate("H", target),
        CliffordTGate("CX", ctrl2, [ctrl1]),
        CliffordTGate("T", ctrl1),
        CliffordTGate("Tdg", ctrl2),
        CliffordTGate("CX", ctrl2, [ctrl1]),
    ]


# ============================================================================
# Intermediate gate (X/CX/CCX) before Clifford+T expansion
# ============================================================================


@dataclass
class ToffoliGate:
    """Intermediate gate before Clifford+T expansion (max 2 controls)."""

    gate_type: str  # "X", "CX", "CCX"
    target: int
    controls: list[int] = field(default_factory=list)

    @property
    def num_controls(self) -> int:
        return len(self.controls)


def expand_to_clifford_t(gates: list[ToffoliGate]) -> list[CliffordTGate]:
    """Expand a list of ToffoliGates into CliffordTGates.

    X -> X (pass through)
    CX -> CX (pass through)
    CCX -> 15 Clifford+T gates
    """
    result = []
    for g in gates:
        if g.gate_type == "X" and g.num_controls == 0:
            result.append(CliffordTGate("X", g.target))
        elif g.gate_type == "CX" and g.num_controls == 1:
            result.append(CliffordTGate("CX", g.target, list(g.controls)))
        elif g.gate_type == "CCX" and g.num_controls == 2:
            result.extend(ccx_to_clifford_t(g.target, g.controls[0], g.controls[1]))
        else:
            raise ValueError(f"Unsupported gate: {g.gate_type} with {g.num_controls} controls")
    return result


# ============================================================================
# Python BK prefix tree implementation (port from ToffoliAdditionCLA.c)
# ============================================================================


def bk_compute_merges(n_carries: int) -> list[tuple[int, int]]:
    """Compute BK prefix tree merge list.

    Port of bk_compute_merges() from ToffoliAdditionCLA.c (lines 58-151).

    Args:
        n_carries: Number of carry positions (bits - 1)

    Returns:
        List of (pos, partner) merge tuples in execution order.
    """
    if n_carries <= 1:
        return []

    merges = []
    up_sweep = [False] * n_carries
    covered = [False] * n_carries
    covered[0] = True  # position 0 is trivially complete

    leftmost = list(range(n_carries))

    # Up-sweep: reduce phase
    max_level = -1
    k = 0
    while True:
        stride = 1 << (k + 1)
        half = 1 << k
        found = False
        pos = stride - 1
        while pos < n_carries:
            partner = pos - half
            if partner >= 0:
                merges.append((pos, partner))
                up_sweep[pos] = True
                leftmost[pos] = leftmost[partner]
                if leftmost[pos] == 0:
                    covered[pos] = True
                found = True
                if k > max_level:
                    max_level = k
            pos += stride
        if not found:
            break
        k += 1

    # Down-sweep: propagate phase
    for dk in range(max_level - 1, -1, -1):
        half = 1 << dk
        stride = half << 1
        pos = stride + half - 1
        while pos < n_carries:
            if not up_sweep[pos]:
                partner = pos - half
                if partner >= 0:
                    merges.append((pos, partner))
                    up_sweep[pos] = True
                    leftmost[pos] = leftmost[partner]
                    if leftmost[pos] == 0:
                        covered[pos] = True
            pos += stride

    # Tail merges: chain from last complete position to uncovered positions
    for i in range(1, n_carries):
        if not covered[i]:
            for j in range(i - 1, -1, -1):
                if covered[j]:
                    merges.append((i, j))
                    leftmost[i] = 0
                    covered[i] = True
                    break

    return merges


