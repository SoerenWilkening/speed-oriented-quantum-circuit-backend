def generate_dispatch_file(min_width: int = 2, max_width: int = 8) -> str:
    """Generate toffoli_clifft_cla_dispatch.c with unified dispatch functions."""
    output = []

    output.append("//")
    output.append("// toffoli_clifft_cla_dispatch.c - Dispatch for BK CLA Clifford+T sequences")
    output.append("//")
    output.append("// Generated by scripts/generate_toffoli_clifft_cla.py - DO NOT EDIT MANUALLY")
    output.append("//")
    output.append("// Routes get_hardcoded_toffoli_clifft_cla_*() to per-width implementations.")
    output.append("// BK CLA requires bits >= 2, so no width-1 entries.")
    output.append("//")
    output.append("")
    output.append('#include "toffoli_sequences.h"')
    output.append("#include <stddef.h>")
    output.append("")

    # Forward declarations for all 4 variants
    variants = [
        ("QQ_add", "QQ add"),
        ("cQQ_add", "cQQ add"),
        ("CQ_inc", "CQ increment (value=1)"),
        ("cCQ_inc", "cCQ increment (value=1)"),
    ]

    for func_suffix, desc in variants:
        output.append(
            "// ============================================================================"
        )
        output.append(f"// FORWARD DECLARATIONS - {desc} (conditionally compiled per width)")
        output.append(
            "// ============================================================================"
        )
        output.append("")
        for w in range(min_width, max_width + 1):
            output.append(f"#ifdef TOFFOLI_SEQ_WIDTH_{w}")
            output.append(
                f"const sequence_t *get_hardcoded_toffoli_clifft_cla_{func_suffix}_{w}(int bits);"
            )
            output.append("#endif")
            output.append("")

    # Dispatch functions
    for func_suffix, desc in variants:
        output.append(
            "// ============================================================================"
        )
        output.append(f"// UNIFIED BK CLA CLIFFORD+T {desc.upper()} DISPATCH")
        output.append(
            "// ============================================================================"
        )
        output.append("")
        output.append(
            f"const sequence_t *get_hardcoded_toffoli_clifft_cla_{func_suffix}(int bits) {{"
        )
        output.append("    switch (bits) {")
        for w in range(min_width, max_width + 1):
            output.append(f"#ifdef TOFFOLI_SEQ_WIDTH_{w}")
            output.append(f"    case {w}:")
            output.append(
                f"        return get_hardcoded_toffoli_clifft_cla_{func_suffix}_{w}(bits);"
            )
            output.append("#endif")
        output.append("    default:")
        output.append("        return NULL;")
        output.append("    }")
        output.append("}")
        output.append("")

    return "\n".join(output)


