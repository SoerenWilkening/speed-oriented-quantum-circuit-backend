def validate(min_width: int = 2, max_width: int = 8) -> bool:
    """Validate generated gate sequences."""
    all_ok = True

    # Expected BK merge counts (verified against C bk_compute_merges())
    expected_merge_counts = {
        2: 0,  # n_carries=1 -> 0 merges
        3: 1,  # n_carries=2 -> 1 merge
        4: 2,  # n_carries=3 -> 2 merges
        5: 4,  # n_carries=4 -> 4 merges
        6: 5,  # n_carries=5 -> 5 merges (up-sweep: 2, down-sweep: 1, tail: 2)
        7: 7,  # n_carries=6 -> 7 merges (up-sweep: 2, down-sweep: 1, tail: 4)
        8: 9,  # n_carries=7 -> 9 merges (up-sweep: 3, down-sweep: 1, tail: 5)
    }

    print("=" * 70)
    print("BK merge count validation")
    print("=" * 70)
    for w in range(min_width, max_width + 1):
        merges = bk_compute_merges(w - 1)
        actual = len(merges)
        expected = expected_merge_counts.get(w)
        if expected is not None:
            status = "OK" if actual == expected else "MISMATCH"
            if actual != expected:
                all_ok = False
        else:
            status = "N/A"
        print(
            f"  width {w:2d}: n_carries={w - 1:2d}, merges={actual:2d}  expected={expected}  [{status}]"
        )
    print()

    # QQ layer counts
    print("=" * 70)
    print("BK CLA QQ Toffoli gate counts")
    print("=" * 70)
    for w in range(min_width, max_width + 1):
        toff_gates = generate_bk_cla_qq_gates(w)
        ccx_count = sum(1 for g in toff_gates if g.gate_type == "CCX")
        cx_count = sum(1 for g in toff_gates if g.gate_type == "CX")
        x_count = sum(1 for g in toff_gates if g.gate_type == "X")
        merges = bk_compute_merges(w - 1)
        num_merges = len(merges)
        expected_layers = 7 * w - 4 + 4 * num_merges
        actual = len(toff_gates)
        status = "OK" if actual == expected_layers else "MISMATCH"
        if actual != expected_layers:
            all_ok = False
        clifft_count = ccx_count * 15 + cx_count + x_count
        clifft_gates = expand_to_clifford_t(toff_gates)
        clifft_status = "OK" if len(clifft_gates) == clifft_count else "MISMATCH"
        if len(clifft_gates) != clifft_count:
            all_ok = False
        print(
            f"  width {w:2d}: {x_count} X + {cx_count} CX + {ccx_count} CCX = {actual} Toffoli "
            f"(expected {expected_layers}) [{status}]  "
            f"-> {len(clifft_gates)} Clifford+T (expected {clifft_count}) [{clifft_status}]"
        )
    print()

    # Verify no CCX in Clifford+T output for all variants
    print("=" * 70)
    print("Gate purity validation (zero CCX in Clifford+T output)")
    print("=" * 70)
    variant_generators = [
        ("QQ", generate_bk_cla_qq_clifft),
        ("cQQ", generate_bk_cla_cqq_clifft),
        ("CQ_inc", generate_bk_cla_cq_inc_clifft),
        ("cCQ_inc", generate_bk_cla_ccq_inc_clifft),
    ]
    for variant_name, gen_func in variant_generators:
        for w in range(min_width, max_width + 1):
            gates = gen_func(w)
            # Check no gate has more than 1 control
            max_ctrl = max(g.num_controls for g in gates) if gates else 0
            # Check all gate types are valid Clifford+T
            valid_types = {"H", "T", "Tdg", "X", "CX"}
            bad_types = {g.gate_type for g in gates} - valid_types
            status = "OK" if max_ctrl <= 1 and not bad_types else "FAIL"
            if max_ctrl > 1 or bad_types:
                all_ok = False
            print(
                f"  {variant_name:8s} width {w:2d}: {len(gates):5d} gates, "
                f"max_ctrl={max_ctrl}, bad_types={bad_types or 'none'}  [{status}]"
            )
    print()

    # Layer count consistency for all variants
    print("=" * 70)
    print("Clifford+T layer counts by variant")
    print("=" * 70)
    for w in range(min_width, max_width + 1):
        qq = generate_bk_cla_qq_clifft(w)
        cqq = generate_bk_cla_cqq_clifft(w)
        cq = generate_bk_cla_cq_inc_clifft(w)
        ccq = generate_bk_cla_ccq_inc_clifft(w)
        print(
            f"  width {w:2d}: QQ={len(qq):5d}  cQQ={len(cqq):5d}  "
            f"CQ_inc={len(cq):5d}  cCQ_inc={len(ccq):5d}"
        )
    print()

    if all_ok:
        print("ALL VALIDATIONS PASSED")
    else:
        print("VALIDATION FAILED - see mismatches above")

    return all_ok


