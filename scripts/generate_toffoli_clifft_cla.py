# BK CLA cQQ Clifford+T generation
# ============================================================================


def generate_bk_cla_cqq_gates(bits: int) -> list[ToffoliGate]:
    """Generate BK CLA cQQ adder gate sequence (controlled by ext_ctrl).

    Derive from QQ sequence by injecting ext_ctrl into every gate:
      X -> CX(target, ext_ctrl)
      CX(target, ctrl) -> CCX(target, ctrl, ext_ctrl)
      CCX(target, c1, c2) -> MCX(3) -> decomposed into 3 CCX via AND-ancilla

    Qubit layout:
      Same as QQ, with ext_ctrl and and_anc appended.
      ext_ctrl = 2*bits + ancilla_count
      and_anc = ext_ctrl + 1
    """
    if bits < 2:
        return []

    n = bits
    n_carries = n - 1
    merges = bk_compute_merges(n_carries)
    num_merges = len(merges)

    ancilla_count = 2 * n_carries + num_merges  # same as bk_cla_ancilla_count
    ext_ctrl = 2 * bits + ancilla_count
    and_anc = ext_ctrl + 1

    qq_gates = generate_bk_cla_qq_gates(bits)
    gates = []

    for g in qq_gates:
        if g.gate_type == "X" and g.num_controls == 0:
            # X -> CX with ext_ctrl
            gates.append(ToffoliGate("CX", g.target, [ext_ctrl]))
        elif g.gate_type == "CX" and g.num_controls == 1:
            # CX -> CCX with [original_ctrl, ext_ctrl]
            gates.append(ToffoliGate("CCX", g.target, [g.controls[0], ext_ctrl]))
        elif g.gate_type == "CCX" and g.num_controls == 2:
            # CCX -> MCX(3) decomposed via AND-ancilla:
            # CCX(and_anc, c1, c2) -- compute AND
            # CCX(target, and_anc, ext_ctrl) -- apply
            # CCX(and_anc, c1, c2) -- uncompute AND
            gates.append(ToffoliGate("CCX", and_anc, [g.controls[0], g.controls[1]]))
            gates.append(ToffoliGate("CCX", g.target, [and_anc, ext_ctrl]))
            gates.append(ToffoliGate("CCX", and_anc, [g.controls[0], g.controls[1]]))
        else:
            raise ValueError(f"Unexpected gate in QQ sequence: {g}")

    return gates


def generate_bk_cla_cqq_clifft(bits: int) -> list[CliffordTGate]:
    """Generate BK CLA cQQ Clifford+T sequence."""
    toffoli_gates = generate_bk_cla_cqq_gates(bits)
    return expand_to_clifford_t(toffoli_gates)


