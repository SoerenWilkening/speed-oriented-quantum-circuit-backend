    if args.width:
        # Single width mode
        if args.width < min_width or args.width > max_width:
            print(f"Error: width must be {min_width}-{max_width}", file=sys.stderr)
            sys.exit(1)
        w = args.width
        if args.dry_run:
            print(f"// === QQ width {w} ===")
            print(generate_qq_file(w))
            print(f"// === cQQ width {w} ===")
            print(generate_cqq_file(w))
            print(f"// === CQ_inc width {w} ===")
            print(generate_cq_inc_file(w))
            print(f"// === cCQ_inc width {w} ===")
            print(generate_ccq_inc_file(w))
        else:
            os.makedirs(output_dir, exist_ok=True)
            for gen_func, fname in [
                (generate_qq_file, f"toffoli_clifft_cla_qq_{w}.c"),
                (generate_cqq_file, f"toffoli_clifft_cla_cqq_{w}.c"),
                (generate_cq_inc_file, f"toffoli_clifft_cla_cq_inc_{w}.c"),
                (generate_ccq_inc_file, f"toffoli_clifft_cla_ccq_inc_{w}.c"),
            ]:
                content = gen_func(w)
                path = os.path.join(output_dir, fname)
                with open(path, "w") as f:
                    f.write(content)
                print(f"Written: {path}")
    else:
        # Full generation mode
        if args.dry_run:
            for w in range(min_width, max_width + 1):
                for gen_func, fname in [
                    (generate_qq_file, f"toffoli_clifft_cla_qq_{w}.c"),
                    (generate_cqq_file, f"toffoli_clifft_cla_cqq_{w}.c"),
                    (generate_cq_inc_file, f"toffoli_clifft_cla_cq_inc_{w}.c"),
                    (generate_ccq_inc_file, f"toffoli_clifft_cla_ccq_inc_{w}.c"),
                ]:
                    print(f"// === FILE: {fname} ===")
                    print(gen_func(w))
                    print()
            print("// === FILE: toffoli_clifft_cla_dispatch.c ===")
            print(generate_dispatch_file(min_width, max_width))
        else:
            os.makedirs(output_dir, exist_ok=True)
            file_count = 0

            for w in range(min_width, max_width + 1):
                for gen_func, fname in [
                    (generate_qq_file, f"toffoli_clifft_cla_qq_{w}.c"),
                    (generate_cqq_file, f"toffoli_clifft_cla_cqq_{w}.c"),
                    (generate_cq_inc_file, f"toffoli_clifft_cla_cq_inc_{w}.c"),
                    (generate_ccq_inc_file, f"toffoli_clifft_cla_ccq_inc_{w}.c"),
                ]:
                    content = gen_func(w)
                    path = os.path.join(output_dir, fname)
                    with open(path, "w") as f:
                        f.write(content)
                    print(f"Written: {path}")
                    file_count += 1

            # Write dispatch file
            dispatch_content = generate_dispatch_file(min_width, max_width)
            dispatch_path = os.path.join(output_dir, "toffoli_clifft_cla_dispatch.c")
            with open(dispatch_path, "w") as f:
                f.write(dispatch_content)
            print(f"Written: {dispatch_path}")
            file_count += 1

            print(f"\nTotal: {file_count} files written")

    return 0


