def _variant_comment(
    variant: str, width: int, gates: list[CliffordTGate], toffoli_gates: list, qubit_desc: str
) -> list[str]:
    """Generate comment block for a variant section."""
    prefix_map = {
        "qq": "TOFFOLI CLIFFT CLA QQ",
        "cqq": "TOFFOLI CLIFFT CLA cQQ",
        "cq_inc": "TOFFOLI CLIFFT CLA CQ_INC",
        "ccq_inc": "TOFFOLI CLIFFT CLA cCQ_INC",
    }
    ccx_count = sum(1 for g in toffoli_gates if g.gate_type == "CCX")
    cx_count = sum(1 for g in toffoli_gates if g.gate_type == "CX")
    x_count = sum(1 for g in toffoli_gates if g.gate_type == "X")
    lines = [
        "// ============================================================================",
        f"// {prefix_map[variant]} WIDTH {width} ({len(gates)} Clifford+T layers)",
        f"// Source: {x_count} X + {cx_count} CX + {ccx_count} CCX = {len(toffoli_gates)} Toffoli gates",
        f"// Clifford+T: {ccx_count}*15 + {cx_count}*1 + {x_count}*1 = {ccx_count * 15 + cx_count + x_count} gates",
        f"// Qubit layout: {qubit_desc}",
        "// All gates: H/T/Tdg/CX/X (zero CCX)",
        "// ============================================================================",
    ]
    return lines


def generate_width_file_qq(width: int) -> tuple[str, list[CliffordTGate]]:
    """Generate QQ section for one width."""
    toffoli_gates = generate_bk_cla_qq_gates(width)
    clifft_gates = expand_to_clifford_t(toffoli_gates)

    n = width
    merges = bk_compute_merges(n - 1)
    num_merges = len(merges)
    tree_base = 3 * n - 1
    carry_base = tree_base + num_merges

    qubit_desc = (
        f"a[0..{n - 1}], b[{n}..{2 * n - 1}], g[{2 * n}..{3 * n - 2}], "
        f"tree_anc[{tree_base}..{tree_base + num_merges - 1 if num_merges > 0 else tree_base}], "
        f"carry[{carry_base}..{carry_base + n - 2}]"
    )

    lines = _variant_comment("qq", width, clifft_gates, toffoli_gates, qubit_desc)
    lines.append("")
    lines.append(generate_static_c(clifft_gates, "TOFFOLI_CLIFFT_CLA_QQ", width))
    lines.append("")

    return "\n".join(lines), clifft_gates


