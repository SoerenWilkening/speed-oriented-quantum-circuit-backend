def generate_static_c(gates: list[CliffordTGate], prefix: str, width: int) -> str:
    """Generate static const C code for a Clifford+T sequence.

    Args:
        gates: List of CliffordTGate
        prefix: Variable name prefix (e.g., "TOFFOLI_CLIFFT_CLA_QQ")
        width: Width for naming
    """
    lines = []
    num_layers = len(gates)

    # Per-layer gate arrays (1 gate per layer)
    for i, gate in enumerate(gates):
        gate_str = gate.to_c_static()
        lines.append(f"static const gate_t {prefix}_{width}_L{i}[] = {{{gate_str}}};")
        lines.append("")

    # Layer pointer array
    if num_layers > 6:
        chunks = [list(range(i, min(i + 6, num_layers))) for i in range(0, num_layers, 6)]
        chunk_lines = []
        for chunk in chunks:
            names = ", ".join(f"{prefix}_{width}_L{j}" for j in chunk)
            chunk_lines.append(f"    {names}")
        layers_str = ",\n".join(chunk_lines)
        lines.append(f"static const gate_t *{prefix}_{width}_LAYERS[] = {{\n{layers_str}}};")
    else:
        names = ", ".join(f"{prefix}_{width}_L{i}" for i in range(num_layers))
        lines.append(f"static const gate_t *{prefix}_{width}_LAYERS[] = {{{names}}};")

    # Gates per layer array (all 1s)
    gpl_str = ", ".join("1" for _ in range(num_layers))
    lines.append(f"static const num_t {prefix}_{width}_GPL[] = {{{gpl_str}}};")
    lines.append("")

    # Sequence struct
    lines.append(
        f"static const sequence_t HARDCODED_{prefix}_{width} = "
        f"{{.seq = (gate_t **){prefix}_{width}_LAYERS,"
    )
    lines.append(f"                                              .num_layer = {num_layers},")
    lines.append(f"                                              .used_layer = {num_layers},")
    lines.append(
        f"                                              .gates_per_layer = "
        f"(num_t *){prefix}_{width}_GPL}};"
    )

    return "\n".join(lines)


