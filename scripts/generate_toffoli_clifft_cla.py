# BK CLA CQ increment (value=1) Clifford+T generation
# ============================================================================


def generate_bk_cla_cq_inc_gates(bits: int) -> list[ToffoliGate]:
    """Generate BK CLA CQ increment (value=1) gate sequence.

    CQ uses temp-register approach: X-init temp to classical value,
    run QQ BK CLA gates (with classical-bit simplification), X-cleanup.

    For value=1 (only LSB=1):
      - Phase A: temp[0]=|1> (X-init), rest =|0>
        - CCX(g[i], temp[i], self[i]) at bit=0: temp[0]=|1> -> CX(g[0], self[0])
        - CCX at bit>0: temp[i]=|0> -> NOP (skip)
        - CX(self[i], temp[i]) at bit=0: temp[0]=|1> -> X(self[0])
        - CX at bit>0: temp[i]=|0> -> NOP (skip)
      - Phase E mirrors Phase A (reverse)
      - Phase F: sum extraction (simplified for value=1)

    Qubit layout (same as C implementation):
      temp[i] = i           (0..n-1)
      self[i] = n+i         (n..2n-1)
      g[i] = 2n+i           (2n..3n-2)
      tree_anc = 3n-1..
      carry_copy = 3n-1+num_merges..
    """
    if bits < 2:
        return []

    n = bits
    n_carries = n - 1
    merges = bk_compute_merges(n_carries)
    num_merges = len(merges)

    tree_base = 3 * n - 1
    carry_base = tree_base + num_merges

    # For value=1: bin = [0,0,...,0,1] MSB-first, so only bit 0 (LSB) is 1
    # bit_i = 1 for i=0, bit_i = 0 for i>0

    gates = []

    # Phase A (simplified for value=1):
    # Generate part: only bit 0 is active
    # bit=0: X-init temp[0], then CX(g[0], self[0])
    gates.append(ToffoliGate("X", 0))  # X(temp[0])
    gates.append(ToffoliGate("CX", 2 * n + 0, [n + 0]))  # CX(g[0], self[0])
    # bits 1..n-2: NOP (temp[i]=|0>)

    # Propagate part: only bit 0 is active
    # bit=0: X(self[0]) (CX with temp[0]=|1> simplifies to X)
    gates.append(ToffoliGate("X", n + 0))  # X(self[0])
    # bits 1..n-1: NOP

    # Phases B, C, D: copied from QQ sequence (all gates unchanged)
    # Build the QQ gates for just B, C, D phases
    prop_src = [n + i for i in range(n_carries)]
    phase_b_gates = []
    tree_anc_idx = 0
    for m_pos, m_partner in merges:
        tree_qubit = tree_base + tree_anc_idx

        g1 = ToffoliGate("CCX", 2 * n + m_pos, [prop_src[m_pos], 2 * n + m_partner])
        gates.append(g1)
        phase_b_gates.append(g1)

        g2 = ToffoliGate("CCX", tree_qubit, [prop_src[m_pos], prop_src[m_partner]])
        gates.append(g2)
        phase_b_gates.append(g2)

        prop_src[m_pos] = tree_qubit
        tree_anc_idx += 1

    # Phase C: Copy carries
    for i in range(n_carries):
        gates.append(ToffoliGate("CX", carry_base + i, [2 * n + i]))

    # Phase D: Reverse Phase B
    for g in reversed(phase_b_gates):
        gates.append(ToffoliGate(g.gate_type, g.target, list(g.controls)))

    # Phase E (simplified, reverse of Phase A for value=1):
    # Propagate uncompute: only bit 0 active
    gates.append(ToffoliGate("X", n + 0))  # X(self[0])

    # Generate uncompute: only bit 0 active
    gates.append(ToffoliGate("CX", 2 * n + 0, [n + 0]))  # CX(g[0], self[0])
    gates.append(ToffoliGate("X", 0))  # X-cleanup(temp[0])

    # Phase F (simplified for value=1):
    # X(self[i]) for bit[i]=1 -> only bit 0
    gates.append(ToffoliGate("X", n + 0))

    # CX(self[i], c[i-1]) for i=1..n-1
    for i in range(1, n):
        gates.append(ToffoliGate("CX", n + i, [carry_base + i - 1]))

    return gates


def generate_bk_cla_cq_inc_clifft(bits: int) -> list[CliffordTGate]:
    """Generate BK CLA CQ increment Clifford+T sequence."""
    toffoli_gates = generate_bk_cla_cq_inc_gates(bits)
    return expand_to_clifford_t(toffoli_gates)


# ============================================================================
# BK CLA cCQ increment (value=1) Clifford+T generation
# ============================================================================


def generate_bk_cla_ccq_inc_gates(bits: int) -> list[ToffoliGate]:
    """Generate BK CLA cCQ increment (value=1) gate sequence.

    Controlled CQ: CX-init (controlled by ext_ctrl) + cQQ BK gates + CX-cleanup.

    For value=1, Phase A becomes:
      - CX-init(temp[0], ext_ctrl) instead of X(temp[0])
      - CCX(g[0], self[0], ext_ctrl) instead of CX(g[0], self[0])
      - CX(self[0], ext_ctrl) instead of X(self[0])

    Phases B, C, D use cQQ gates (all controlled by ext_ctrl).

    Phase E mirrors Phase A.
    Phase F:
      - CX(self[0], ext_ctrl) instead of X(self[0])
      - CCX(self[i], c[i-1], ext_ctrl) instead of CX(self[i], c[i-1])

    Qubit layout:
      Same as CQ, with ext_ctrl and and_anc appended.
    """
    if bits < 2:
        return []

    n = bits
    n_carries = n - 1
    merges = bk_compute_merges(n_carries)
    num_merges = len(merges)

    ancilla_count = 2 * n_carries + num_merges
    ext_ctrl = 2 * bits + ancilla_count
    and_anc = ext_ctrl + 1

    gates = []

    tree_base = 3 * n - 1
    carry_base = tree_base + num_merges

    # Phase A (simplified + controlled for value=1):
    # bit=0 only active:
    gates.append(ToffoliGate("CX", 0, [ext_ctrl]))  # CX-init(temp[0], ext_ctrl)
    gates.append(ToffoliGate("CCX", 2 * n + 0, [n + 0, ext_ctrl]))  # CCX(g[0], self[0], ext_ctrl)
    gates.append(ToffoliGate("CX", n + 0, [ext_ctrl]))  # CX(self[0], ext_ctrl)

    # Phases B, C, D: use cQQ gates (inject ext_ctrl into every QQ gate)
    # Build the QQ gates for B, C, D, then inject ext_ctrl
    prop_src = [n + i for i in range(n_carries)]
    phase_b_qq_gates = []
    tree_anc_idx = 0
    for m_pos, m_partner in merges:
        tree_qubit = tree_base + tree_anc_idx

        g1 = ToffoliGate("CCX", 2 * n + m_pos, [prop_src[m_pos], 2 * n + m_partner])
        phase_b_qq_gates.append(g1)

        g2 = ToffoliGate("CCX", tree_qubit, [prop_src[m_pos], prop_src[m_partner]])
        phase_b_qq_gates.append(g2)

        prop_src[m_pos] = tree_qubit
        tree_anc_idx += 1

    # Phase C QQ gates
    phase_c_qq_gates = []
    for i in range(n_carries):
        phase_c_qq_gates.append(ToffoliGate("CX", carry_base + i, [2 * n + i]))

    # Phase D QQ gates (reverse of Phase B)
    phase_d_qq_gates = list(reversed(phase_b_qq_gates))

    # Inject ext_ctrl into all B+C+D QQ gates
    all_bcd_qq = phase_b_qq_gates + phase_c_qq_gates + phase_d_qq_gates
    for g in all_bcd_qq:
        if g.gate_type == "CX" and g.num_controls == 1:
            # CX -> CCX with ext_ctrl
            gates.append(ToffoliGate("CCX", g.target, [g.controls[0], ext_ctrl]))
        elif g.gate_type == "CCX" and g.num_controls == 2:
            # CCX -> MCX(3) decomposed via AND-ancilla
            gates.append(ToffoliGate("CCX", and_anc, [g.controls[0], g.controls[1]]))
            gates.append(ToffoliGate("CCX", g.target, [and_anc, ext_ctrl]))
            gates.append(ToffoliGate("CCX", and_anc, [g.controls[0], g.controls[1]]))
        else:
            raise ValueError(f"Unexpected gate in BCD: {g}")

    # Phase E (simplified + controlled, reverse of Phase A):
    gates.append(ToffoliGate("CX", n + 0, [ext_ctrl]))  # CX(self[0], ext_ctrl)
    gates.append(ToffoliGate("CCX", 2 * n + 0, [n + 0, ext_ctrl]))  # CCX uncompute
    gates.append(ToffoliGate("CX", 0, [ext_ctrl]))  # CX-cleanup(temp[0], ext_ctrl)

    # Phase F (simplified + controlled for value=1):
    gates.append(ToffoliGate("CX", n + 0, [ext_ctrl]))  # controlled X(self[0])
    for i in range(1, n):
        gates.append(ToffoliGate("CCX", n + i, [carry_base + i - 1, ext_ctrl]))

    return gates


def generate_bk_cla_ccq_inc_clifft(bits: int) -> list[CliffordTGate]:
    """Generate BK CLA cCQ increment Clifford+T sequence."""
    toffoli_gates = generate_bk_cla_ccq_inc_gates(bits)
    return expand_to_clifford_t(toffoli_gates)


