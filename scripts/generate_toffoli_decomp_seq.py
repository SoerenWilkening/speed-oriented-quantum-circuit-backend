#!/usr/bin/env python3
"""Toffoli MCX-decomposed hardcoded sequence generation (widths 1-8).

This script generates per-width C source files (toffoli_decomp_seq_N.c) and a
unified dispatch file (toffoli_decomp_seq_dispatch.c) for MCX-decomposed
controlled quantum-quantum (cQQ) CDKM addition sequences.

Unlike generate_toffoli_seq.py which produces cQQ sequences with MCX(3 controls)
requiring dynamic allocation, this script decomposes every MCX(3) into 3 CCX
gates via the AND-ancilla pattern. The resulting sequences use only X/CX/CCX
(max 2 controls) and can be fully static const.

AND-ancilla MCX decomposition (Phase 74-03):
  MCX(target, [c1, c2, c3]) ->
    CCX(and_anc, c1, c2)     -- compute partial AND
    CCX(target, and_anc, c3)  -- apply via AND-ancilla
    CCX(and_anc, c1, c2)     -- uncompute AND

Gate encoding:
  - X:   .Gate=X, .Target=t, .NumControls=0, .Control={0}, .GateValue=1
  - CX:  .Gate=X, .Target=t, .NumControls=1, .Control={c}, .GateValue=1
  - CCX: .Gate=X, .Target=t, .NumControls=2, .Control={c1,c2}, .GateValue=1

Algorithm: CDKM ripple-carry adder (Cuccaro et al., 2004)
  Controlled variant with AND-ancilla MCX decomposition:
  - cMAJ(a,b,c,ext,and_anc) = CCX(b,c,ext), CCX(a,c,ext),
      CCX(and_anc,a,b), CCX(c,and_anc,ext), CCX(and_anc,a,b)
  - cUMA(a,b,c,ext,and_anc) =
      CCX(and_anc,a,b), CCX(c,and_anc,ext), CCX(and_anc,a,b),
      CCX(a,c,ext), CCX(b,a,ext)

Qubit layout for width N (N >= 2):
  [0..N-1]   = register a (target)
  [N..2N-1]  = register b (source)
  [2N]       = carry ancilla
  [2N+1]     = external control qubit
  [2N+2]     = AND-ancilla
  Total: 2N+3 qubits

Usage:
  python scripts/generate_toffoli_decomp_seq.py                  # Generate all files
  python scripts/generate_toffoli_decomp_seq.py --width 4        # Generate only width 4
  python scripts/generate_toffoli_decomp_seq.py --dry-run        # Print to stdout
  python scripts/generate_toffoli_decomp_seq.py --validate       # Validate layer counts

Generated by: scripts/generate_toffoli_decomp_seq.py
Phase 74, Plan 05: Hardcoded MCX-decomposed sequences
"""

