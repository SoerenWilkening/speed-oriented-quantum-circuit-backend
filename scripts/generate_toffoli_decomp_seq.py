# CDKM algorithm: decomposed controlled gate sequences
# ============================================================================


def generate_decomp_cqq_gates(bits: int) -> list[ToffoliGate]:
    """Generate MCX-decomposed controlled QQ CDKM adder gate sequence.

    Qubit layout:
      [0..bits-1]       = register a (target)
      [bits..2*bits-1]  = register b (source)
      [2*bits]          = ancilla carry (bits >= 2)
      [2*bits+1]        = external control qubit
      [2*bits+2]        = AND-ancilla (bits >= 2)

    Width 1: [0]=a, [1]=b, [2]=ext_control. Single CCX. No AND-ancilla needed.
    Width N >= 2: decomposed cMAJ/cUMA chain. Total qubits: 2N+3.
    """
    gates = []

    if bits == 1:
        # Single CCX: controlled a[0] ^= b[0]
        gates.append(ToffoliGate("X", target=0, controls=[1, 2]))
        return gates

    ancilla = 2 * bits
    ext_ctrl = 2 * bits + 1
    and_anc = 2 * bits + 2

    # Forward decomposed cMAJ sweep
    gates.extend(_cmaj_decomp(ancilla, bits + 0, 0, ext_ctrl, and_anc))
    for i in range(1, bits):
        gates.extend(_cmaj_decomp(i - 1, bits + i, i, ext_ctrl, and_anc))

    # Reverse decomposed cUMA sweep
    for i in range(bits - 1, 0, -1):
        gates.extend(_cuma_decomp(i - 1, bits + i, i, ext_ctrl, and_anc))
    gates.extend(_cuma_decomp(ancilla, bits + 0, 0, ext_ctrl, and_anc))

    return gates


def _cmaj_decomp(a: int, b: int, c: int, ext: int, and_anc: int) -> list[ToffoliGate]:
    """Decomposed cMAJ(a, b, c, ext, and_anc):

    Step 1: CCX(target=b, ctrl1=c, ctrl2=ext)
    Step 2: CCX(target=a, ctrl1=c, ctrl2=ext)
    Step 3a: CCX(target=and_anc, ctrl1=a, ctrl2=b)  -- compute AND
    Step 3b: CCX(target=c, ctrl1=and_anc, ctrl2=ext) -- apply
    Step 3c: CCX(target=and_anc, ctrl1=a, ctrl2=b)  -- uncompute AND
    """
    return [
        ToffoliGate("X", target=b, controls=[c, ext]),
        ToffoliGate("X", target=a, controls=[c, ext]),
        ToffoliGate("X", target=and_anc, controls=[a, b]),
        ToffoliGate("X", target=c, controls=[and_anc, ext]),
        ToffoliGate("X", target=and_anc, controls=[a, b]),
    ]


def _cuma_decomp(a: int, b: int, c: int, ext: int, and_anc: int) -> list[ToffoliGate]:
    """Decomposed cUMA(a, b, c, ext, and_anc):

    Step 1a: CCX(target=and_anc, ctrl1=a, ctrl2=b)  -- compute AND
    Step 1b: CCX(target=c, ctrl1=and_anc, ctrl2=ext) -- apply
    Step 1c: CCX(target=and_anc, ctrl1=a, ctrl2=b)  -- uncompute AND
    Step 2:  CCX(target=a, ctrl1=c, ctrl2=ext)
    Step 3:  CCX(target=b, ctrl1=a, ctrl2=ext)
    """
    return [
        ToffoliGate("X", target=and_anc, controls=[a, b]),
        ToffoliGate("X", target=c, controls=[and_anc, ext]),
        ToffoliGate("X", target=and_anc, controls=[a, b]),
        ToffoliGate("X", target=a, controls=[c, ext]),
        ToffoliGate("X", target=b, controls=[a, ext]),
    ]


def _gate_description(gate: ToffoliGate) -> str:
    """Human-readable gate description for comments."""
    if gate.num_controls == 0:
        return f"X(target={gate.target})"
    elif gate.num_controls == 1:
        return f"CX(target={gate.target}, ctrl={gate.controls[0]})"
    elif gate.num_controls == 2:
        return f"CCX(target={gate.target}, ctrl1={gate.controls[0]}, ctrl2={gate.controls[1]})"
    else:
        ctrls = ",".join(str(c) for c in gate.controls)
        return f"MCX(target={gate.target}, ctrls=[{ctrls}])"


