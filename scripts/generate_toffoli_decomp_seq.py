@dataclass
class ToffoliGate:
    """Represents a single gate in a Toffoli sequence (max 2 controls)."""

    gate_type: str  # "X" (with controls: CX, CCX)
    target: int
    controls: list[int] = field(default_factory=list)

    @property
    def num_controls(self) -> int:
        return len(self.controls)

    def to_c_static(self) -> str:
        """Generate C static const initializer (only for gates with <= 2 controls)."""
        if self.num_controls > 2:
            raise ValueError(f"Cannot generate static const for {self.num_controls} controls")

        if self.num_controls == 0:
            return (
                f"{{.Gate = X,\n"
                f"                                      .Target = {self.target},\n"
                f"                                      .NumControls = 0,\n"
                f"                                      .Control = {{0}},\n"
                f"                                      .large_control = NULL,\n"
                f"                                      .GateValue = 1,\n"
                f"                                      .NumBasisGates = 0}}"
            )
        elif self.num_controls == 1:
            return (
                f"{{.Gate = X,\n"
                f"                                      .Target = {self.target},\n"
                f"                                      .NumControls = 1,\n"
                f"                                      .Control = {{{self.controls[0]}}},\n"
                f"                                      .large_control = NULL,\n"
                f"                                      .GateValue = 1,\n"
                f"                                      .NumBasisGates = 0}}"
            )
        else:  # num_controls == 2
            return (
                f"{{.Gate = X,\n"
                f"                                      .Target = {self.target},\n"
                f"                                      .NumControls = 2,\n"
                f"                                      .Control = {{{self.controls[0]}, {self.controls[1]}}},\n"
                f"                                      .large_control = NULL,\n"
                f"                                      .GateValue = 1,\n"
                f"                                      .NumBasisGates = 0}}"
            )


