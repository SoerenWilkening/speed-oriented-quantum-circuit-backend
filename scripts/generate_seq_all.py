@dataclass
class Gate:
    gate_type: str  # H, P, X
    target: int
    control: int | None
    value: float

    def to_c(self) -> str:
        if self.gate_type == "H":
            return f"""{{.Gate = H,
                                      .Target = {self.target},
                                      .NumControls = 0,
                                      .Control = {{0}},
                                      .large_control = NULL,
                                      .GateValue = 0,
                                      .NumBasisGates = 0}}"""
        elif self.gate_type == "P":
            return f"""{{.Gate = P,
                                      .Target = {self.target},
                                      .NumControls = 1,
                                      .Control = {{{self.control}}},
                                      .large_control = NULL,
                                      .GateValue = {self.format_angle()},
                                      .NumBasisGates = 0}}"""
        elif self.gate_type == "X":
            return f"""{{.Gate = X,
                                      .Target = {self.target},
                                      .NumControls = 1,
                                      .Control = {{{self.control}}},
                                      .large_control = NULL,
                                      .GateValue = 1,
                                      .NumBasisGates = 0}}"""

    def format_angle(self) -> str:
        """Format angle as SEQ_PI expression."""
        if self.value == 0:
            return "0"

        # Try to express as simple fraction of PI
        ratio = self.value / math.pi

        # Check common fractions
        for denom in [1, 2, 4, 8, 16, 32, 64, 128, 256]:
            for numer in range(-denom * 2, denom * 2 + 1):
                if numer == 0:
                    continue
                if abs(ratio - numer / denom) < 1e-10:
                    if numer == 1 and denom == 1:
                        return "SEQ_PI"
                    elif numer == -1 and denom == 1:
                        return "-SEQ_PI"
                    elif numer == 1:
                        return f"SEQ_PI / {denom}"
                    elif numer == -1:
                        return f"-SEQ_PI / {denom}"
                    elif denom == 1:
                        return f"{numer} * SEQ_PI"
                    else:
                        return f"{numer} * SEQ_PI / {denom}"

        # Fallback to decimal
        return f"{self.value}"


