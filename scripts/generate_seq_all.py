def validate(max_width: int = 8) -> bool:
    """Cross-validate script output against expected layer counts for widths 1-8."""

    # Expected layer counts from existing generated C files (verified reference)
    expected_qq = {1: 3, 2: 8, 3: 14, 4: 23, 5: 35, 6: 50, 7: 68, 8: 89}
    expected_cqq = {1: 7, 2: 15, 3: 26, 4: 40, 5: 57, 6: 77, 7: 100, 8: 126}

    all_ok = True

    print("=" * 60)
    print("QQ_add layer count validation (widths 1-8)")
    print("=" * 60)
    for w in range(1, max_width + 1):
        layers = optimize_layers(generate_qq_add(w))
        actual = len(layers)
        expected = expected_qq[w]
        gate_count = sum(len(layer) for layer in layers)
        status = "OK" if actual == expected else "MISMATCH"
        if actual != expected:
            all_ok = False
        print(
            f"  width {w:2d}: {actual:4d} layers ({gate_count:4d} gates)  expected {expected:4d}  [{status}]"
        )

    print()
    print("=" * 60)
    print("cQQ_add layer count validation (widths 1-8)")
    print("=" * 60)
    for w in range(1, max_width + 1):
        layers = optimize_layers(generate_cqq_add(w))
        actual = len(layers)
        expected = expected_cqq[w]
        gate_count = sum(len(layer) for layer in layers)
        status = "OK" if actual == expected else "MISMATCH"
        if actual != expected:
            all_ok = False
        print(
            f"  width {w:2d}: {actual:4d} layers ({gate_count:4d} gates)  expected {expected:4d}  [{status}]"
        )

    print()
    print("=" * 60)
    print("CQ_add template layer count validation (widths 1-16)")
    print("=" * 60)
    for w in range(1, 17):
        layers = generate_cq_add_template_layers(w)
        actual = len(layers)
        expected = 5 * w - 2
        status = "OK" if actual == expected else "MISMATCH"
        if actual != expected:
            all_ok = False
        print(f"  width {w:2d}: {actual:4d} layers  expected {expected:4d} (5*{w}-2)  [{status}]")

    print()
    print("=" * 60)
    print("cCQ_add template layer count validation (widths 1-16)")
    print("=" * 60)
    for w in range(1, 17):
        layers = generate_ccq_add_template_layers(w)
        actual = len(layers)
        expected = 5 * w - 2
        status = "OK" if actual == expected else "MISMATCH"
        if actual != expected:
            all_ok = False
        print(f"  width {w:2d}: {actual:4d} layers  expected {expected:4d} (5*{w}-2)  [{status}]")

    # Verify cQQ_add Block 2 uses bits+bit as control (not external control)
    print()
    print("=" * 60)
    print("cQQ_add Block 2 control verification")
    print("=" * 60)
    for w in range(1, max_width + 1):
        layers = generate_cqq_add(w)  # Unoptimized to inspect structure
        control = 2 * w
        # Find Block 2 negative half-rotation gates (between CNOTs)
        # Block 2 starts after QFT + Block 1
        qft_count = sum(1 + max(0, t) for t in range(w - 1, -1, -1))
        block1_count = w
        block2_start = qft_count + block1_count
        # Check that negative rotations use bits+bit, not control
        block2_ok = True
        for bit in range(w - 1, -1, -1):  # noqa: B007
            # Skip CNOT, check rotation gates, skip CNOT
            # In unoptimized layers, we look at each P gate in Block 2
            for layer in layers[block2_start:]:
                for g in layer:
                    if g.gate_type == "P" and g.value < 0:
                        if g.control == control:
                            block2_ok = False
                            print(
                                f"  width {w}: FAIL - Block 2 uses external control {control} for negative rotation"
                            )
                            break
        if block2_ok:
            print(f"  width {w}: OK - Block 2 uses b-register qubit as control")

    print()
    if all_ok:
        print("ALL VALIDATIONS PASSED")
    else:
        print("VALIDATION FAILED - see mismatches above")

    return all_ok


