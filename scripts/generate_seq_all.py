def main():
    parser = argparse.ArgumentParser(
        description="Generate hardcoded addition sequence C files for widths 1-16."
    )
    parser.add_argument(
        "--width", type=int, metavar="N", help="Generate only for width N (default: all 1-16)"
    )
    parser.add_argument(
        "--dry-run", action="store_true", help="Print to stdout instead of writing files"
    )
    parser.add_argument(
        "--output-dir",
        type=str,
        default=None,
        help="Output directory (default: c_backend/src/sequences/)",
    )
    parser.add_argument(
        "--validate", action="store_true", help="Cross-validate against expected layer counts"
    )
    parser.add_argument(
        "--max-width", type=int, default=16, help="Maximum width to generate (default: 16)"
    )

    args = parser.parse_args()

    if args.validate:
        ok = validate()
        sys.exit(0 if ok else 1)

    # Determine output directory
    if args.output_dir:
        output_dir = args.output_dir
    else:
        # Default: relative to script location
        script_dir = os.path.dirname(os.path.abspath(__file__))
        output_dir = os.path.join(script_dir, "..", "c_backend", "src", "sequences")
        output_dir = os.path.normpath(output_dir)

    max_width = args.max_width

    if args.width:
        # Single width mode
        if args.width < 1 or args.width > max_width:
            print(f"Error: width must be 1-{max_width}", file=sys.stderr)
            sys.exit(1)
        content = generate_width_file(args.width)
        if args.dry_run:
            print(content)
        else:
            os.makedirs(output_dir, exist_ok=True)
            path = os.path.join(output_dir, f"add_seq_{args.width}.c")
            with open(path, "w") as f:
                f.write(content)
            print(f"Written: {path}")
    else:
        # Full generation mode: all per-width files + dispatch
        if args.dry_run:
            # In dry-run mode, print all files separated by markers
            for w in range(1, max_width + 1):
                print(f"// === FILE: add_seq_{w}.c ===")
                print(generate_width_file(w))
                print()
            print("// === FILE: add_seq_dispatch.c ===")
            print(generate_dispatch_file(max_width))
        else:
            os.makedirs(output_dir, exist_ok=True)
            for w in range(1, max_width + 1):
                content = generate_width_file(w)
                path = os.path.join(output_dir, f"add_seq_{w}.c")
                with open(path, "w") as f:
                    f.write(content)
                print(f"Written: {path}")

            dispatch_content = generate_dispatch_file(max_width)
            dispatch_path = os.path.join(output_dir, "add_seq_dispatch.c")
            with open(dispatch_path, "w") as f:
                f.write(dispatch_content)
            print(f"Written: {dispatch_path}")

    return 0


if __name__ == "__main__":
    sys.exit(main() or 0)
