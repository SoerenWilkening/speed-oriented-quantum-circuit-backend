def _generate_qft_layers(bits: int) -> list[list[Gate]]:
    """Generate QFT layers matching the C QFT() function layout.

    The C QFT() produces exactly 2*bits-1 layers with gate packing:
    For j=0..n-1, qubit q=n-1-j:
      - H(q) placed at layer 2*j
      - CP(q, q-i-1, pi/2^(i+1)) placed at layer 2*j+i+1 for i=0..n-2-j

    Multiple gates can share the same layer when they operate on independent qubits.
    """
    n = bits
    total = 2 * n - 1
    layers: list[list[Gate]] = [[] for _ in range(total)]

    for j in range(n):
        q = n - 1 - j
        layers[2 * j].append(Gate("H", q, None, 0))
        for i in range(n - 1 - j):
            angle = math.pi / (2 ** (i + 1))
            layers[2 * j + i + 1].append(Gate("P", q, q - i - 1, angle))

    return layers


def _generate_iqft_layers(bits: int) -> list[list[Gate]]:
    """Generate inverse QFT layers matching the C QFT_inverse() function layout.

    The C QFT_inverse() produces exactly 2*bits-1 layers (reverse of QFT).
    For j=0..n-1, qubit q=n-1-j:
      - CP(q, q-i-1, -pi/2^(i+1)) at layer (2*n-1) - (2*j+i+1) - 1
      - H(q) at layer (2*n-1) - 2*j - 1
    """
    n = bits
    total = 2 * n - 1
    layers: list[list[Gate]] = [[] for _ in range(total)]

    for j in range(n):
        q = n - 1 - j
        for i in range(n - 1 - j):
            layer_idx = total - (2 * j + i + 1) - 1
            angle = -math.pi / (2 ** (i + 1))
            layers[layer_idx].append(Gate("P", q, q - i - 1, angle))
        h_layer = total - 2 * j - 1
        layers[h_layer].append(Gate("H", q, None, 0))

    return layers


def generate_cq_add_template_layers(bits: int) -> list[list[Gate]]:
    """Return the gate structure for CQ_add template matching C QFT layout.

    Structure:
      - QFT on target register [0, bits-1] (2*bits-1 layers, matching C QFT())
      - bits rotation layers: each has one P gate on qubit i with angle placeholder 0
      - Inverse QFT on target register (2*bits-1 layers, matching C QFT_inverse())
      - Total layers: 5 * bits - 2
    """
    layers = []

    # QFT section (2*bits-1 layers)
    layers.extend(_generate_qft_layers(bits))

    # Rotation layers: one P gate per qubit with placeholder angle 0
    for i in range(bits):
        layers.append([Gate("P", i, None, 0)])  # Uncontrolled P (angle injected at runtime)

    # IQFT section (2*bits-1 layers)
    layers.extend(_generate_iqft_layers(bits))

    return layers


def _generate_gate_literal(gate: Gate, is_rotation_placeholder: bool = False) -> str:
    """Generate a (gate_t) compound literal for use in init functions."""
    if gate.gate_type == "H":
        return (
            f"(gate_t){{.Gate = H, .Target = {gate.target}, .NumControls = 0, "
            f".Control = {{0}}, .large_control = NULL, .GateValue = 0, .NumBasisGates = 0}}"
        )
    elif gate.gate_type == "P":
        if gate.control is not None:
            # Controlled P (CP gate)
            angle_str = "0" if is_rotation_placeholder else gate.format_angle()
            return (
                f"(gate_t){{.Gate = P, .Target = {gate.target}, .NumControls = 1, "
                f".Control = {{{gate.control}}}, .large_control = NULL, "
                f".GateValue = {angle_str}, .NumBasisGates = 0}}"
            )
        else:
            # Uncontrolled P gate (rotation placeholder in CQ_add template)
            return (
                f"(gate_t){{.Gate = P, .Target = {gate.target}, .NumControls = 0, "
                f".Control = {{0}}, .large_control = NULL, .GateValue = 0, .NumBasisGates = 0}}"
            )


def generate_cq_add_template_c(bits: int) -> str:
    """Generate C function init_hardcoded_CQ_add_N() for template-init pattern.

    The template pre-allocates the full sequence structure. QFT and IQFT gates have
    fixed angles. Rotation layers get GateValue = 0 (placeholder -- caller injects angles).
    """
    layers = generate_cq_add_template_layers(bits)
    n = bits
    total_layers = len(layers)
    qft_layers = 2 * bits - 1  # Number of QFT layers
    rotation_start = qft_layers
    rotation_end = rotation_start + bits

    lines = []
    lines.append(f"static sequence_t *cached_CQ_add_{n} = NULL;")
    lines.append("")
    lines.append(f"sequence_t *init_hardcoded_CQ_add_{n}(void) {{")
    lines.append(f"    if (cached_CQ_add_{n} != NULL) return cached_CQ_add_{n};")
    lines.append("")
    lines.append("    sequence_t *seq = malloc(sizeof(sequence_t));")
    lines.append("    if (seq == NULL) return NULL;")
    lines.append(f"    seq->num_layer = {total_layers};")
    lines.append(f"    seq->used_layer = {total_layers};")
    lines.append(f"    seq->gates_per_layer = calloc({total_layers}, sizeof(num_t));")
    lines.append(f"    seq->seq = calloc({total_layers}, sizeof(gate_t *));")
    lines.append("    if (seq->gates_per_layer == NULL || seq->seq == NULL) {")
    lines.append("        free(seq->gates_per_layer); free(seq->seq); free(seq); return NULL;")
    lines.append("    }")

    for i, layer in enumerate(layers):
        num_gates = len(layer)
        lines.append(f"    // Layer {i}")
        lines.append(f"    seq->gates_per_layer[{i}] = {num_gates};")
        lines.append(f"    seq->seq[{i}] = calloc({num_gates}, sizeof(gate_t));")
        is_rotation = rotation_start <= i < rotation_end
        for j, gate in enumerate(layer):
            literal = _generate_gate_literal(gate, is_rotation_placeholder=is_rotation)
            lines.append(f"    seq->seq[{i}][{j}] = {literal};")

    lines.append(f"    cached_CQ_add_{n} = seq;")
    lines.append("    return seq;")
    lines.append("}")

    return "\n".join(lines)


