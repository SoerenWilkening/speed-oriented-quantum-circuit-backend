#!/usr/bin/env python3
"""Unified generation script for all hardcoded addition sequences (widths 1-16).

This script generates per-width C source files (add_seq_N.c) and a unified dispatch
file (add_seq_dispatch.c) for the hardcoded sequence infrastructure.

It replaces the two earlier scripts:
  - generate_seq_1_4.py (DEPRECATED - kept for reference only)
  - generate_seq_5_8.py (DEPRECATED - kept for reference only)

Generated files contain four addition variants per width:
  - QQ_add:  Quantum-quantum addition (static const sequence)
  - cQQ_add: Controlled quantum-quantum addition (static const sequence)
  - CQ_add:  Classical-quantum addition template (init function with placeholder angles)
  - cCQ_add: Controlled classical-quantum addition template (init function with placeholder angles)

Usage:
  python scripts/generate_seq_all.py                     # Generate all 16 per-width files + dispatch
  python scripts/generate_seq_all.py --width 5           # Generate only add_seq_5.c
  python scripts/generate_seq_all.py --dry-run            # Print to stdout, don't write files
  python scripts/generate_seq_all.py --dry-run --width 8  # Print width 8 to stdout
  python scripts/generate_seq_all.py --validate           # Cross-validate against expected layer counts
  python scripts/generate_seq_all.py --output-dir /tmp    # Write to custom directory

CRITICAL: cQQ_add Block 2 uses bits+bit (b-register qubit) as control for negative
half-rotations, NOT the external control qubit. This is the fix from quick-015 (c891a32).

Generated by: scripts/generate_seq_all.py
"""

