def generate_width_file(width: int) -> str:
    """Generate complete C source file for one width."""
    output = []

    # Header
    output.append("//")
    output.append(f"// add_seq_{width}.c - Hardcoded addition sequences for {width}-bit width")
    output.append("//")
    output.append("// Generated by scripts/generate_seq_all.py - DO NOT EDIT MANUALLY")
    output.append("//")
    output.append("// Contains: QQ_add, cQQ_add (static const), CQ_add, cCQ_add (template-init)")
    output.append("//")
    output.append("")
    output.append('#include "sequences.h"')
    output.append("")
    output.append(f"#ifdef SEQ_WIDTH_{width}")
    output.append("")
    output.append("#include <stdlib.h>")
    output.append("")
    output.append("// SEQ_PI as compile-time constant for static initializers")
    output.append("// (math.h M_PI is not a constant expression in standard C)")
    output.append("#ifndef SEQ_PI")
    output.append("#define SEQ_PI 3.14159265358979323846")
    output.append("#endif")
    output.append("")

    # QQ_ADD section
    qq_layers = generate_qq_add(width)
    qq_layers = optimize_layers(qq_layers)
    output.append("// ============================================================================")
    output.append(f"// QQ_ADD WIDTH {width} ({len(qq_layers)} layers)")
    output.append(f"// Qubit layout: [0,{width - 1}] = target, [{width},{2 * width - 1}] = control")
    output.append("// ============================================================================")
    output.append("")
    output.append(generate_c_sequence("QQ_ADD", qq_layers, width))
    output.append("")

    # cQQ_ADD section
    cqq_layers = generate_cqq_add(width)
    cqq_layers = optimize_layers(cqq_layers)
    control_qubit = 2 * width
    output.append("// ============================================================================")
    output.append(f"// cQQ_ADD WIDTH {width} ({len(cqq_layers)} layers)")
    output.append(
        f"// Qubit layout: [0,{width - 1}] = target, [{width},{2 * width - 1}] = b, "
        f"[{control_qubit}] = control"
    )
    output.append("// ============================================================================")
    output.append("")
    output.append(generate_c_sequence("cQQ_ADD", cqq_layers, width))
    output.append("")

    # CQ_ADD template-init section
    cq_layers = generate_cq_add_template_layers(width)
    output.append("// ============================================================================")
    output.append(f"// CQ_ADD WIDTH {width} (template-init, {len(cq_layers)} layers)")
    output.append(f"// Qubit layout: [0,{width - 1}] = target, classical value injected at runtime")
    output.append("// ============================================================================")
    output.append("")
    output.append(generate_cq_add_template_c(width))
    output.append("")

    # cCQ_ADD template-init section
    ccq_layers = generate_ccq_add_template_layers(width)
    output.append("// ============================================================================")
    output.append(f"// cCQ_ADD WIDTH {width} (template-init, {len(ccq_layers)} layers)")
    output.append(
        f"// Qubit layout: [0,{width - 1}] = target, [{width}] = control, "
        f"classical value injected at runtime"
    )
    output.append("// ============================================================================")
    output.append("")
    output.append(generate_ccq_add_template_c(width))
    output.append("")

    # Per-width dispatch helpers
    output.append("// ============================================================================")
    output.append(f"// DISPATCH HELPERS FOR WIDTH {width}")
    output.append("// ============================================================================")
    output.append("")
    output.append(f"const sequence_t *get_hardcoded_QQ_add_{width}(int bits) {{")
    output.append("    (void)bits;")
    output.append(f"    return &HARDCODED_QQ_ADD_{width};")
    output.append("}")
    output.append("")
    output.append(f"const sequence_t *get_hardcoded_cQQ_add_{width}(int bits) {{")
    output.append("    (void)bits;")
    output.append(f"    return &HARDCODED_cQQ_ADD_{width};")
    output.append("}")
    output.append("")
    output.append(f"sequence_t *get_hardcoded_CQ_add_{width}(int bits) {{")
    output.append("    (void)bits;")
    output.append(f"    return init_hardcoded_CQ_add_{width}();")
    output.append("}")
    output.append("")
    output.append(f"sequence_t *get_hardcoded_cCQ_add_{width}(int bits) {{")
    output.append("    (void)bits;")
    output.append(f"    return init_hardcoded_cCQ_add_{width}();")
    output.append("}")
    output.append("")

    output.append(f"#endif // SEQ_WIDTH_{width}")
    output.append("")

    return "\n".join(output)


